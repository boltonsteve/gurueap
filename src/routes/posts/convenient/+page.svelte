    <h1>Convenient</h1>

    <p class="font-italic ml-3">Physical shops are more <span class="font-weight-bold text-danger">convenient</span> than online stores. Firstly, in physical shops you pay less money because you can bargain with the seller. Secondly, shopping in physical shops can be a social activity.</p>

<p>There is a category of physical store aptly named <b><i>convenience stores</i></b>. Many countries have 7 Elevens. In Indonesia we have Indomaret, Alfamart and Circle K.</p>

<p>Indonesians might call a shop that sells everything at a low price <b><i>convenient</i></b>. However, the prices in <b><i>convenience stores</i></b> like Circle K can be quite a lot higher than average. These shops inflate prices precisely so that they can offer the following <b><i>conveniences</i></b>:</p>

<ul>
	<li>they are numerous, especially in cities</li>
	<li>they have ample parking if they are situated on a road</li>
	<li>they can even be found inside large shopping centres</li>
	<li>they stock items that most people need on a daily basis</li>
	<li>they provide fast and efficient service</li>
</ul>

<p>These are all features that most people would consider <b><i>convenient</i></b>. In English if something saves you time and effort then it is <b><i>convenient</i></b>.</p>

    <div class="alert alert-danger mb-1">
        <p>The following are <span class="uli">NOT</span> <b><i>conveniences</i></b>:</p>
        <ul>
            <li>You can meet your friends in the shop.</li>
            <li>You save money on certain items.</li>
        </ul>
    </div>

