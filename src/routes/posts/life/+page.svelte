<h3>2. People's lives</h3>

<p>In our opening example there is a grammatical error -Â <span class="font-weight-bold font-italic text-danger">people's life</span>. <b><i>People</i></b> is plural and <b><i>life</i></b> is singular - so it doesn't make sense to put those words together!</p>

<p>And again, it is always assumed you are talking about <b><i>people</i></b> (NOT aliens, etc) and the things that they experience while they are alive (NOT after they are dead), <span class="uli">unless otherwise stated</span>.</p>

<ul>
    <li class="font-italic">Chocolate is delicious.</li>
    <li class="small mb-1 lst-none">(at any stage of life, as long as you're alive and not dead!)</li>
