    <h1>Goodbye to 'By'</h1>

    <p class="ml-3 font-italic"><span class="font-weight-bold text-danger">By</span> paying more attention to corruption can improve the welfare of a country.</p>

    <p>Yet another Indonesian structure that doesn't translate directly into English!</p>

    <p>If you really must begin with 'by' then you need...</p>

    <p><b>By + [name of solution] + comma + subject + verb (+ etc):</b></p>

    <p class="ml-3 font-italic"><span class="font-weight-bold text-success">By</span> paying more attention to corruption, a government can improve the welfare of a country.</p>

    <p>However, native speakers would probably just say "Goodbye to 'By'" and go straight to the solution as the theme in the sentence:</p>

    <p class="ml-3 font-italic"><span class="font-weight-bold text-success">Paying</span> more attention to corruption can improve the welfare of a country.</p>

