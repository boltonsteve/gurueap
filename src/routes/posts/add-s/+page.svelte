    <h1>Add 's' - by George</h1>

    <p>{@html flagIndo} Indonesians hate to add 's' to <b><i>plurals</i></b>, <b><i>possessives</i></b> and <b><i>third person verbs</i></b>. In Bahasa Indonesia these grammatical features are produced in other ways.</p>

    <p>{@html flagIndo} It's also extremely unusual in Bahasa Indonesia to see <b><i>two or more consonants together</i></b>, which is often what happens when you add 's' to the end of a word:</p>

    <ul>
        <li><i><b>Mike's </b></i>(possessive, 2 consonants together)</li>
        <li><i><b>expands</b></i> (third person, 3 consonants together)</li>
        <li><i><b>texts</b></i> (plural, 4 consonants together!)</li>
    </ul>

    <p>Pronouncing this final 's' is difficult for Indonesians and for some reason embarrassing, rather like when English people attempt to pronounce the French 'r'.</p>

    <p>But if you want to communicate well, and if you want a good score for pronunciation in <b><i>IELTS speaking</i></b>, then you had better start producing the 's' at word endings!</p>

    <p>In this video, former student George does his best to put 's' in all the right places. I've added a scoring feature to help you follow his 'performance'!</p>

    <p>A good way to practice 's' is to record yourself, and then listen back following a tapescript. Focus on the 's' in particular. Exaggerate it. Make it longer and louder. In the IELTS test make sure the examiner can hear it!</p>

