    <h1>Directions: 'pass'</h1>

    <div class="alert alert-danger font-italic">The fallopian tube is a tunnel which <span class="font-weight-bold text-danger">is passed by</span> the sperm on its way to the egg.</div>

    <p>This might be possible if all the sperm does is pass by the entrance to the tunnel without actually entering it. But then it won’t be on its way to the egg, it will be on its way somewhere else!</p>

    <p>If you want to say that the sperm <i>enters</i> the tunnel, <i>travels along it</i> and <i>then meets the egg</i>, you need to use <span class="font-weight-bold text-primary">a preposition</span> that carries that meaning:</p>

    <div class="alert alert-success font-italic">The fallopian tube is a tunnel which the sperm passes <span class="font-weight-bold text-primary">through</span> on its way to the egg.</div>

    <img src="/img/tunnel.png" class="img-fluid mx-auto" alt="tunnel" style="width:75%;">

    <p>Compare these situations:</p>

    <ul>
        <li>A to B: The sperm <b><i>passes through</i></b> the tunnel.</li>
        <li>C to D: The tunnel <b><i>is passed by</i></b> the sperm.</li>
        <li>D to E: The tunnel <b><i>is passed by</i></b> the sperm.</li>
    </ul>

