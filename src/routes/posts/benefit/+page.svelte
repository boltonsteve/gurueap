    <h3>Benefit – verb</h3>

    <div class="alert alert-success font-italic">Constructing impressive buildings <span class="text-success font-weight-bold">benefits</span> visitors more than local people.</div>

    <p>The verb <b><i>benefit</i></b> is transitive, no preposition. Notice the position of <b><i>more</i></b> in the comparison!</p>

    <h3>Beneficial – adjective</h3>

    <div class="alert alert-success font-italic">Constructing impressive buildings is more <span class="text-success font-weight-bold">beneficial for</span> visitors than for local people.</div>

    <p>The adjective <b><i>beneficial</i></b> may be followed by a preposition phrase – usually <b><i>beneficial + for</i></b> (except “When attempting to lose weight it is more beneficial to exercise than to diet.”).</p>

    <p>Without a comparative you might also write:</p>

    <div class="alert alert-success font-italic">Constructing impressive buildings is <span class="text-success font-weight-bold">beneficial</span>.</div>

    <h3>Benefit – noun</h3>

    <div class="alert alert-success font-italic">The <span class="text-success font-weight-bold">benefits to</span> visitors <span class="text-success font-weight-bold">of</span> constructing impressive buildings are greater than the <span class="text-success font-weight-bold">benefits to</span> local people.</div>

    <p>The noun <b><i>benefit</i></b> – when applied to people (visitors) – is followed by <b><i>to<b><i>.</p>

    <p>When applied to things (constructing impressive buildings) it is followed by <b><i>of<b><i>.</p>


