    <h3>Concern - Adjective</h3>
    <p>You can (not) <b><i>be concerned</i></b> about <span class="uli">nothing in particular</span>:</p>

<div class="alert alert-success font-italic">The government <span class="font-weight-bold text-success">is</span> not <span class="font-weight-bold text-success">concerned</span>.</div>

<p>And you can (not) <b><i>be concerned</i></b> about <span class="uli">a particular thing</span>:</p>

<div class="alert alert-success font-italic">The government <span class="font-weight-bold text-success">is</span> not <span class="font-weight-bold text-success">concerned about</span> <span class="uli">the crime rate in cities</span>.</div>

<p>If something is the object of concern, then remember to use the preposition: <b><i>concerned about</i></b> <span class="uli">something</span>.</p>

<h3>Concern - Noun</h3>

<p>It's also possible to say the same thing using <b><i>concern</i></b> as a noun, where <b><i>concern</i></b> often collocates with another word (in this case, 'cause'):</p>

<div class="alert alert-success font-italic">The government does not consider the crime rate in cities to be a <span class="uli">cause for</span> <span class="font-weight-bold text-success">concern</span>.</div>

<h3>Concern - Verb</h3>

<p>And it's also possible to use the verb form (notice the auxiliary verb 'to do' in the construction of the negative!):</p>

<div class="alert alert-success font-italic">The crime rate in cities <span class="uli">does not</span> <span class="font-weight-bold text-success">concern</span> the government.</div>

<h3>Which form of 'concern' is best?</h3>

<p>So which one to use? Adjective? Noun? Verb? Well if you can remember to put __ed onto concern and add about, it's probably best to use the adjective form!</p>

