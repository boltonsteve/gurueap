    <p>Nice try! The IELTS examiner will understand that you are trying to make an adjective from the verb <b><i>fluctuate</i></b> using <b><i>ive</i></b>. Normally this would be a good strategy, but there is no such word as <b><i>fluctuative</i></b>!</p>

    <p>The safest approach is to use the verb form:</p>

    <div class="alert alert-success font-italic">Since the abolition of fuel subsidies, prices <span class="font-weight-bold text-success">have fluctuated</span>.</div>

    <p>Alternatively, you can make an adjective phrase using the noun form and featuring some collocation:</p>

    <div class="alert alert-success font-italic">Since the abolition of fuel subsidies, prices have been <span class="font-weight-bold text-success">subject to fluctuation</span>.</div>

    <p>Finally you might try some even more sophisticated academic collocation:</p>

    <div class="alert alert-success font-italic">Since the abolition of fuel subsidies, prices have <span class="font-weight-bold text-success">tended to fluctuate</span>.</div>

    <p>Now go ahead and remove <b><i>fluctuative</i></b> from your list of ‘ive’ adjectives!</p>

