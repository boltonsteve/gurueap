    <h1>The blah blah sector</h1>

    <p class="font-italic alert alert-danger"><span class="font-weight-bold text-danger">Agricultural sector</span> is different from <span class="font-weight-bold text-danger">economic sector</span> in the way research is conducted.</p>

    <h3>Definite reference ('the')</h3>

    <p>In the phrase 'agricultural sector', <b><i>agricultural</i></b> tells us <span class="uli">this sector, exactly</span>.</p>

    <p>In English grammar this is called 'definite reference'. Both writer and reader understand exactly which thing is being referred to - in this case, <b><i>this sector exactly</i></b> and not some other sector.</p>

    <p>If you want to communicate <b><i>this exactly</i></b> then you need to use the definite article - 'the':</p>

    <p class="font-italic ml-3"><span class="font-weight-bold text-success"><span class="uli">The</span> agricultural sector</span> is different from <span class="font-weight-bold text-success"><span class="uli">the</span> economic sector</span> in the way research is conducted.</p>

    <p>If you do not use 'the' when you mean this one exactly then you will receive a low score in IELTS for grammar and also perhaps for coherence and cohesion.</p>

    <p>If you do not use 'the' when you mean <b><i>this one exactly</i></b> then your reader will stop reading and think <i>"Does he mean <b>this one exactly</b>, or does he mean <b>one of many</b>, or does he mean <b>all of them everywhere</b>?"</i> You must communicate one of these meanings if you want to be understood clearly.</p>

    <h3>Indefinite reference ('a/an')</h3>

    <p>If you want to communicate <b><i>one of many</i></b> then you need to use the indefinite article -  'a/an':</p>

    <p class="font-italic ml-3">Agriculture is <span class="font-weight-bold text-success"><span class="uli">a</span> sector</span> that requires different research approaches.</p>

    <h3>Generic reference ('s')</h3>

    <p>If you want to communicate <b><i>all of them everywhere</i></b>, and your noun is countable, then you need to use the plural - 's':</p>

    <p class="font-italic ml-3"><span class="font-weight-bold text-success">Government sector<span class="uli">s</span></span> mentioned in the report include agriculture and economics.</p>

