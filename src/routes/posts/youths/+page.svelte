    <h1>Youths and young people</h1>

    <div class="alert alert-danger font-italic">These days <span class="font-weight-bold text-danger">youths</span> are more challenged by the future because making decisions about the future is not easy.</div>

    <p>This is grammatically correct but inappropriate.</p>

    <p>The countable noun <b>youth </b>tends to have negative connotations, especially when it's plural:</p>

    
    <div class="alert alert-success font-italic"><span class="font-weight-bold text-success">Youths</span> at football matches often cause trouble.</div>

    <p><b>Youths</b> are troublemakers, at that awkward age between childhood and adulthood when they rebel against authority and indulge in sex, drugs and rock and roll, often with negative consequences.</p>

    <p><b>Youths</b> hang around town in gangs and old ladies are afraid of them.</p>

    <p>The lexical phrase <b>the youth of today</b> is also usually negative:</p>

    
    <div class="alert alert-success font-italic"><span class="font-weight-bold text-success">The youth of today</span> have no respect for others.</div>

    <hr>

    <h3>Positive about young people</h3>

    <img src="/img/young-people.png" class="img-fluid rounded" alt="young people">

    <p>Most of the time in IELTS Task 2 essays you want to maintain a more positive - or at least neutral - attitude to young people, and so it's probably best to refer to them as exactly that - <span class="font-weight-bold text-success">young people</span>!</p>

    <div class="alert alert-success font-italic">These days <span class="font-weight-bold text-success">young people</span> are more challenged by the future because making decisions about the future is not easy.</div>

    <p>Another option for IELTS writing would be:</p>

    <div class="alert alert-success font-italic">These days <span class="font-weight-bold text-success">the younger generation</span> are more challenged by the future because making decisions about the future is not easy.</div>

    <p>In IELTS Task 2 you also often want to make a prediction about how a situation may affect young people in the future. In this case you are talking about <span class="font-weight-bold text-success">future generations</span>:</p>

    <div class="alert alert-success font-italic">
        <p class="mt-0">Global warming is a problem that governments need to solve for the sake of <span class="font-weight-bold text-success">future generations</span>.</p>
        <p class="mb-0"><span class="font-weight-bold text-success">Future generations</span> will prosper as long as they follow a healthy lifestyle.</p>
    </div>

    <p>Notice that we assume there will be more than one future generation and if we're generalising then there is no article (the).</p>

