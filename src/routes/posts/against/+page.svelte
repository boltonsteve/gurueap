    <p>In English, <b><i>against</i></b> is a preposition, and since the opening sentence does not contain a verb it is therefore not a sentence. To make a sentence, you can do this:</p>

    <div class="alert alert-success font-italic">Most Indonesian people <span class="font-weight-bold text-success">are against</span> the removal of fuel subsidies.<br><span class="small">(to be + against)</span></div>

    <p class="pl-2">or (slightly more academic) this..</p>

    <div class="alert alert-success font-italic">Most Indonesian people <span class="font-weight-bold text-success">oppose</span> the removal of fuel subsidies.</div>

    <p class="pl-2">or (also academic) this..</p>

    <div class="alert alert-success font-italic mb-2">Most Indonesian people <span class="font-weight-bold text-success">object to</span> the removal of fuel subsidies.</div>

