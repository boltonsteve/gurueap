    <div class="alert alert-danger font-italic">Interacting with many people expands <span class="font-weight-bold text-danger">students vision</span> and broadens their horizons.</div>

<h3>1. Plural noun + Noun</h3>
<p class="font-italic ml-3">students vision</p>
<p>This is not possible. It is not grammatically correct.</p>

<h3>2. Singular noun + Noun</h3>
<p class="font-italic ml-3">student vision</p>
<ul>
    <li>The first noun gives information about the second noun.</li>
    <li class="lst-none mb-1"><b><i>student</i></b> tells us what kind of <b><i>vision</i></b></li>
    <li class="mb-1">The reference to the first noun is impersonal and abstract.</li>
    <li class="mb-1">Usually the first noun is inanimate (not a living thing or a group of living things):</li>
    <li class="font-italic lst-none ml-3">I can't find my <b>pencil case</b>.</li>
</ul>

<h3>3. Plural noun + possessive + Noun</h3>
<p class="font-italic ml-3">students' vision</p>
<ul>
    <li>The first noun tells us who possesses the second noun.</li>
    <li class="lst-none small mb-1">(the vision that is possessed by students)</li>
    <li class="mb-1">The reference to the first noun is personal and concrete.</li>
    <li class="mb-1">The first noun is nearly always animate (a living thing or group of living things):</li>
    <li class="font-italic lst-none ml-3"><b>Men's magazines</b> are  displayed beyond the reach of children.</li>
</ul>


<p>Returning to our opening example, meaning 3 would appear to be the most appropriate:</p>

