    <h1>It's depend!</h1>

    <div class="alert alert-danger font-italic mb-1">Happiness <span class="font-weight-bold text-danger">is depend</span> on a person’s view of their life.</div>

    <WithIcon icon="warning" position="right" msg="Students often mistakenly add <b><i>to be</i></b> to V1 to make present simple tense. It could be that they have seen other structures using <b><i>to be</i></b> and apply the same ‘rules’ to present simple verb forms." />

    <p>Let’s take a look at some structures that use <b><i>to be</i></b> and think about those that do not.</p>

    <h2>‘to be’ + adjective</h2>

    <div class="alert alert-success">
        <ul class="pl-0 my-0 font-italic" style="list-style-type:none;">
            <li>Roses are red.</li>
            <li>I’m tired.</li>
            <li>Isn’t it hot today? (Positive = It <b>is</b> hot today.)</li>
        </ul>
    </div>

    <p>In these examples, adjectives give information about nouns:</p>

    <ul class="my-0" style="list-style-type:none;">
        <li><b><i>red</i></b> tells us about <b><i>Roses</i></b></li>
        <li><b><i>tired</i></b> tells us about <b><i>I</i></b></li>
    </ul>

    <p>Notice that the verb <b><i>to be</i></b> needs to 'agree' with the subject:</p>

    <ul class="mt-0 mb-2" style="list-style-type:none;">
        <li><b><i>Are</i></b> agrees with <b><i>Roses</i></b> (3rd person plural)</li>
        <li><b><i>Am</i></b> agrees with <b><i>I</i></b> (1st person singular)</li>
    </ul>

    <WithIcon icon="info" position="right" msg='If you are not familiar with the conjugation of verbs (1st person, 2nd person, etc.), see <a href="https://www.grammarly.com/blog/verb-conjugation/" target="_blank">here</a>.' />

    <h2>‘to be’ + noun</h2>

<div class="alert alert-success">
    <ul class="pl-0 my-0 font-italic" style="list-style-type:none;">
        <li>I’m a doctor.</li>
        <li>These chairs are office chairs.</li>
        <li>Indonesia is an Asian country.</li>
    </ul>
</div>

<p>In these examples, nouns give information about other nouns:</p>

    <ul class="my-0 font-italic" style="list-style-type:none;">
        <li><b><i>doctor</i></b> gives information about <b><i>I</i></b></li>
        <li><b><i>office chairs</i></b> gives information about <b><i>these chairs</i></b></li>
    </ul>

    <p>Notice again that in each example the verb <b><i>to be</i></b> 'agrees' with the subject:</p>

    <ul class="my-0" style="list-style-type:none;">
        <li>
            <b><i>Am</i></b> agrees with <b><i>I</i></b> (1st person singular)
        </li>
    </ul>

    <h2>‘to be’ + preposition phrase</h2>

    <div class="alert alert-success">
        <ul class="pl-0 my-0 font-italic" style="list-style-type:none;">
            <li>He’s in his office.</li>
            <li>The chairs are on the back of the truck.</li>
            <li>Indonesia is in south-east Asia.</li>
        </ul>
    </div>

    <p>In these examples preposition phrases give information about nouns:</p>

    <ul class="my-0" style="list-style-type:none;">
        <li><b><i>in his office</i></b> tells us about <b><i>He</i></b></li>
        <li><b><i>on the back of the truck</i></b> tells us about <b><i>The chairs</i></b></li>
    </ul>

    <h2 class="text-danger">‘to be’ + verb</h2>

    <p>In our opening example, <span class="text-danger font-weight-bold">to be</span> is put before the verb <span class="text-primary font-weight-bold">depend</span> (V1).</p>

    <div class="alert alert-danger font-italic">Happiness <span class="font-weight-bold text-danger">is</span> <span class="font-weight-bold text-primary">depend</span> on a person’s view of their life.</div>

    <p>This is incorrect! The only time <b><i>to be</i></b> appears before a verb is <span class="uli">when the form of the verb is continuous</span>:</p>

    <div class="alert alert-success mb-2">
        <ul class="pl-0 my-0 font-italic" style="list-style-type:none;">
            <li>My brother <span class="text-danger font-weight-bold">is</span> prepar<span class="text-primary font-weight-bold">ing</span> to sit the IELTS test.</li>
            <li>This time next week I <span class="text-danger font-weight-bold">will be</span> sitting on a beach sipp<span class="text-primary font-weight-bold">ing</span> martinis.</li>
            <li>In 2005 they <span class="text-danger font-weight-bold">were</span> liv<span class="text-primary font-weight-bold">ing</span> in Australia.</li>
        </ul>
    </div>

    <WithIcon icon="info" position="right" msg="Notice the tense may be past, present or future!<br>And always make sure that subjects ‘agree’ with verbs:<ul><li>My brother (singular) + 'is' (NOT 'are')</li><li>they (plural) + 'were' (NOT 'was')</li></ul>" />

</div>



