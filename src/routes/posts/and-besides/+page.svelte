    <h1>Besides...  And besides...</h1>

    <p>These are often used inaccurately as they don’t translate well from other languages.</p>

    <h2 class="mb-0">Besides</h2>
    <p class="mt-0">Let’s use <em><b>besides</b></em> to modify the following argument:</p>

    <p class="ml-3 font-italic"><em>I don’t think we should go to the cinema tonight. First of all I don’t like the film. Secondly, there is an unusual amount of traffic in town. Finally, we don’t have any money.</em></p>

    <p>Here we have three supporting claims for <b><i>not</i></b> wanting to see the film:</p>

    <ol>
        <li>I don’t like the film.</li>
        <li>The traffic in town is heavy.</li>
        <li>We don’t have any money.</li>
    </ol>

    <p>The same argument could be expressed using <em><b>besides</b></em>, as follows:</p>

    <p class="ml-3 font-italic">I don’t think we should go to the cinema tonight. <b>Besides</b> not liking the film and the unusual amount of traffic in town, we don’t have any money.</p>

    <p>The second sentence (the supports) can be represented:</p>

    <p class="ml-3"><i><b>Besides</b></i> + claim(s) [nouns] + , + final claim [a sentence].</p>

    <p>In this case <i><b>besides</b></i> simply means <i><b>as well as</b></i>.</p>

    <h2 class="mb-0">And besides</h2>
    <p class="mt-0">Here the meaning is a little different:</p>

    <p class="ml-3 font-italic">I don’t think we should go to the cinema tonight. First of all I don’t like the film. Secondly, there is an unusual amount of traffic in town. <b>And besides</b>, we don’t have any money.</p>

    <p>The supporting claim introduced by <em><b>and besides</b></em> is much stronger than the preceding supporting claims. In fact, it is so strong that it is really not necessary to consider the previous claims. If we have no money then there’s no way we can go to the cinema!</p>

    <p>Again, it’s useful to diagram the structure:</p>

    <p class="ml-3">Weak claim(s) + <em><b>And besides</b></em> + very strong [and final!] claim</p>

    <p>Here the meaning is more than just <i><b>as well as</b></i>.</p>
    <p><i><b>And besides</b></i> introduces a very powerful claim that makes all other preceding claims redundant.</p>

