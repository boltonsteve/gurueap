    </div>
</WithIcon>
    
<h3>Current = <i><b>up to and including now</b></i></h3>

<ul class="lh-15">
 	<li><i>Social media <b><span class="text-success">have become</span></b> the most common media to be used to share information.</i></li>
 	<li><i>Social media <b><span class="text-success">have become</span></b> the most common media to be used to share information <b><span class="text-success">recently</span></b>.</i></li>
 	<li><i>Social media <b><span class="text-success">have recently become</span></b> the most common media to be used to share information.</i></li>
</ul>

<p>All <b><i>present perfect</i></b> tense!</p>

<p>Notice that present perfect tense means 'up to and including now', and so you don't have to use a time expression.</p>

<p><b><i>Recently</i></b> is a strong <b><i>present perfect</i></b> time expression and if you don't believe me, browse over and check out countless other <a href="https://skell.sketchengine.eu/#result?lpos=&query=recently&lang=en&f=concordance" target="_blank" rel="noopener">examples at forbetterenglish.com</a>.</p>

<h3>Conclusion</h3>

<p>Returning to our opening example, clearly our writer wants to communicate either <b><i>now</i></b> or <b><i>around now</i></b>, and so <b><i>recently</i></b> is not the most appropriate time expression! My advice would be to use <b><i>present simple + today</i></b>:</p>

<ul>
 	<li><i>Social media <b><span class="text-success">are today</span></b> the most common media used to share information.</i></li>
</ul>

