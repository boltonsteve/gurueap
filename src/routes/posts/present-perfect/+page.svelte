    <h1>Have you used present perfect <i>recently?</i></h1>

        <div class="alert alert-danger font-italic">Social media <span class="font-weight-bold text-danger">are</span> the most common media used to share information <span class="font-weight-bold text-danger">recently</span>.</div>

    <p>This looks like as though it might function as the opening sentence of an IELTS Task 2 essay. Let's first of all remind ourselves about some ideal features for the IELTS Task 2 opening sentence:</p>

<ol class="lh-15">
 	<li>We're <b><i>introducing the topic</i></b> as stated on the IELTS question paper.</li>
 	<li>We're thinking about our reader and what might be <b><i>interesting</i></b> to him/her. (Our reader is an educated non-specialist, someone quite educated, who reads a lot and knows what's happening in the world.)</li>
 	<li>We're relating the topic to a <b><i>current</i></b> situation or event, because <b><i>current</i></b> situations or events are automatically interesting.</li>
 	<li>We're relating the topic to a <b><i>local context</i></b>, because that's also interesting to our reader, and because we know more about local than about distant contexts.</li>
</ol>

<p>Even without seeing the question paper we can see that our writer has generally <i>ticked these boxes</i>. Now let's focus on the word <i><b>recently</b></i>. That's a good word to use as it helps to tick box number 3 - it helps us to focus on a <i><b>current</b></i> feature of the topic. However, we need to choose time expressions and matching tenses carefully if we want to communicate <b><i>current</i></b>.</p>

<h3>Current = <i><b>now</b></i></h3>

<ul class="lh-15">
 	<li><i>Social media <b><span class="text-success">are</span></b> the most common media used to share information.</i></li>
 	<li><i>Social media <b><span class="text-success">are today</span></b> the most common media used to share information.</i></li>
 	<li><i><b><span class="text-success">These days</span></b> social media <b><span class="text-success">are</span></b> the most common media used to share information.</i></li>
</ul>

<p>All <b><i>present simple</i></b> tense!</p>

<p>Notice that present simple tense can mean <b><i>now</i></b>, and so you don't have to use a time expression.</p>

<h3>Current = <i><b>around now</b></i></h3>

<ul class="lh-15">
 	<li><i>Social media <b><span class="text-success">are becoming</span></b> the most common media used to share information.</i></li>
 	<li><i>Social media <b><span class="text-success">are today becoming</span></b> the most common media used to share information.</i></li>
 	<li><i><b><span class="text-success">These days</span></b> social media <b><span class="text-success">are becoming</span></b> the most common media used to share information.</i></li>
</ul>

<p>All <b><i>present continuous</i></b> tense!</p>

<p>Notice that present continuous tense can mean <b><i>around now</i></b>, and so you don't have to use a time expression.</p>

<p>Notice also that <b><i>to be</i></b> cannot be continuous. In this case it is easy to use 'become' in the continuous, <i>but only if you feel the situation is changing over time</i>.</p>

