    <h1><i>Even</i> is not <i>Bahkan</i></h1>

    <p class="ml-3 font-italic">Motorcyclists in Bali don't seem to care about their own safety or other people's. They weave in and out of traffic without leaving room to manoevre. They cut in front of cars and then brake hard. They ride on the pavement and on the wrong side of the road. <span class="font-weight-bold text-danger">Even</span> they don't wear helmets.</p>

    <p>As in the example above, <b><i>bahkan</i></b> is often translated as <b><i>even</i></b>. However, whereas in Indonesian <b><i>bahkan</i></b> is positioned at the beginning of the sentence, in English <b><i>even</i></b> (meaning bahkan) is positioned in front of the verb:</p>

    <p class="ml-3 font-italic">They don't <span class="font-weight-bold text-success">even</span> wear helmets.</p>

    <p>If you put <b><i>even</i></b> (meaning bahkan) at the beginning of the sentence, the IELTS examiner will understand you but you will get a low score for grammar. Many people might also be confused, because <b><i>even</i></b> is often used in English at the beginning of a sentence together with <i><b>though</b></i>:</p>

    <p class="ml-3 font-italic"><span class="font-weight-bold text-success">Even though</span> it is illegal not to wear a helmet, Balinese motorcyclists take their helmets off whenever they can.</p>

    <p>In this example, <b><i>even</i></b> is a part of <b><i>even though</i></b>, and no longer carries the meaning of <b><i>bahkan</i></b>.</p>

