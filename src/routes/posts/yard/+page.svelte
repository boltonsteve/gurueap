    <p class="mt-0">In English, <b><i>wide</i></b> is one of several dimensions (including <b><i>long</i></b>, <b><i>deep</i></b>, etc.), and doesn't really communicate the idea of overall size.</p>

    <p>If you tell me your yard is <b><i>wide</i></b>, I immediately want to know whether it is <b><i>long</i></b>. Then I might be able to decide whether it is <b><i>big</i></b> or <b><i>small</i></b>. For example, a yard might be 10m <b><i>wide</i></b>, but only 10cm <b><i>long</i></b>.</p>

    <p>To communicate the idea of overall size – when speaking about the land <i>next to</i>, <i>between</i>, or <i>surrounding</i> buildings – it would be better to say:</p>

    <div class="alert alert-success font-italic">Of course I love my house. It has a yard. Actually it’s not a very <span class="text-success font-weight-bold">big</span> yard.</div>

