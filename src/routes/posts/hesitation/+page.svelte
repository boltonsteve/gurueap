    <h1>Hesitation</h1>

    <p>While <i>too much</i> hesitation in IELTS speaking can bring down your score for <b><i><a href="../posts/ielts-descriptors" target="blank">Fluency and Coherence (FC)</a></i></b>, some hesitation is unavoidable.</p>

    <p>There is bad hesitation and good hesitation.</p>

    <h3>Er.. Uhm..</h3>

    <p>These are useful some of the time, but try to keep them to a minimum.</p>

    <h3>Silence before speaking</h3>

    When the examiner asks a question, do not start answering immediately unless you actually have something to say.


    It's quite common for native speakers to wait a few seconds - in silence - before answering.

    This will give you time to formulate your answer, or at least a large part of your answer. 


