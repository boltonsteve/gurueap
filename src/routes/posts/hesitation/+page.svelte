    <h1>Hesitation and Fluency in IELTS Speaking</h1>

    The IELTS speaking test includes some challenging questions, especially in Part 3 of the interview.

    <p>Sometimes hesitation is difficult to avoid, but there is a right way and a wrong way to hesitate when you're struggling to maintain fluency.</p>

    <h3 class="mb-0">1. Silence</h3>
    <p class="mt-0">When the examiner asks a question, do not start answering immediately unless you are confident that you are ready with an answer.</p>

    <p>Don't do this:</p>

    <p class="font-italic ml-3">I believe that <b><i>[pause for 3 seconds]</i></b> it is very difficult to stamp out corruption completely.</p>

    <p>Instead, do this:</p>

    <p class="font-italic ml-3"><b><i>[pause for 3 seconds]</i></b> I believe that it is very difficult to stamp out corruption completely.</p>

    <p>By giving yourself time before answering, you are more likely to produce a complete sentence wihout any more hesitation.</p>

    <p>If you really need to pause mid-sentence, then at least give your listener meaningful sentence <b><i>chunks</i></b>.</p>

    <p>The chunk <i>I believe that</i> - on its own - does not carry very much meaning, and the listener has to hold it in his/her head as he/she waits for the next chunk.</p>

    <p>Longer, more meaningful chunks make it easier for the listener to complete - mentally - a sentence that contains hesitation.</p>

    <p class="mb-0">Compare:</p>

    <div class="grid1 mb-0">
        <p>Chunks that are difficult to join in order to complete the sentence mentally</p>
        <p>Chunks that are easy to join mentally - <i>even if there are longer silences in between</i></p>
        <ul class="my-0 pl-2">
            <li>I believe that (pause)</li>
            <li>it is very (pause)</li>
            <li>difficult to (pause)</li>
            <li>stamp out (pause)</li>
            <li>corruption (pause)</li>
            <li>completely.</li>
        </ul>
        <ul class="my-0 pl-2">
            <li>I believe that it is very difficult (pause)</li>
            <li>to stamp out corruption (pause)</li>
            <li>completely.</li>
        </ul>
    </div>


    <h3 class="mb-0">2. Fillers</h3>
    <p class="mt-0">Instead of silence, native speakers often use <b><i>fillers</i></b> as a way to 'buy time' while they are thinking about how to answer a question or how to continue an idea.</p>

    <h5 class="mb-0">Er.. Uhm..</h5>
    <p class="mt-0">These are useful some of the time, and you will certainly hear native speakers using them. However, try to keep them to a minimum. At least try to combine them with other, more sophisticated fillers.</p>

    <h3 class="mb-0">More sophisticated fillers</h3>
    <p class="mt-0">Before we look at some fillers that native speakers might use, remember you must use them <b><i>appropriately</i></b>. Your choice of filler will sometimes depend on the type of question and/or the content of your answer.</p>

    <p>The following would <span class="uli">not</span> be <b><i>appropriate</i></b>:</p>

    <div class="grid2 my-1">
        <div class="font-weight-bold">Examiner</div>
        <div class="font-italic">Do you have any brothers or sisters?</div>
        <div class="font-weight-bold">Candidate</div>
        <div class="font-italic">Hmm. That's an interesting question. Yes, I have a brother and eight sisters.</div>
    </div>

    <p>In this exchange, there is <i>no excuse for hesitation</i> - you should know by now whether or not you have any brothers or sisters. Also, there is nothing <i>interesting</i> about this question.</p>

    <p>Fillers that you might hear from a native speaker..</p>

    <p>..in reponse to a question:</p>

    <ul class="lh-15">
        <li><i>Well. Hmm. Let me see.</i></li>
        <li><i>Hmm..</i> [2 seconds] <i>Interesting..</i> [1 second]</li>
        <li><i>Well..</i> [2 seconds] <i>I suppose that..</i></li>
        <li><i>Hmm.. I (must admit I) hadn't really thought about that before.. But if I had to choose, then I would say..</i></li>
        <li><i>Ok let's see..</i> [3 seconds]</li>
        <li><i>That's a tough one..</i> [2 seconds]</li>
        <li><i>I suppose it depends (on)..</i> [2 seconds]</li>
        <li><i>Well, if by blah blah you mean blah blah, then I would have to say..</i></li>
    </ul>
   
    <p>..pausing before continuing an idea or line of thought:</p>

    <ul class="lh-15">
        <li><i>and..</i> [3 seconds]</li>
        <li><i>another point to consider might be..</i></li>
        <li><i>that's one thing / another thing to consider.. In addition..</i></li>
        <li>[2 seconds] .. <i>at least that's what a lot of people believe these days..</i> [2 seconds] <i>and you might also say that..</i></li>
        <li><i>secondly</i> [2 seconds] <i>I suppose it could also be said that..</i></li>
    </ul>

    <p>Actually when developing your ideas, almost any signal - together with a pause - will behave as a <b><i>filler</i></b> and will buy you time while you think about what to say next.</p>

    <p><b><i>Fillers</i></b> need to sound natural, so make sure you have memorised them, and make sure you are using them appropriately. The whole point of using fillers is that you don't have to think about them - you should be thinking about what you're going to say next <span class="uli">while you are using the filler</span>!</p>

    <hr>
    
    <p class="mt-0 small">Remember that <i>too much</i> hesitation in IELTS speaking can bring down your score for <b><i><a href="../posts/ielts-descriptors" target="blank">Fluency and Coherence (FC)</a></i></b>.</p>

