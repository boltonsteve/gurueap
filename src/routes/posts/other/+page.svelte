    <div class="alert alert-success font-italic">Bakso was chosen by 60% of students, Martabak by 20%, Siomay by 15%, and only 5% chose <span class="font-weight-bold text-success">other kinds of Asian fast food</span>.</div>

    <WithIcon icon="info" position="right">
        <div slot="html"><b><i>Other</i></b> is used as a noun in <a href="https://en.wikipedia.org/wiki/Other" target="_blank" rel="noopener">sociology</a>, <a href="http://psychology.wikia.com/wiki/Other" target="_blank" rel="noopener">psychology</a> and <a href="http://www.anthropologymatters.com/index.php/anth_matters/article/view/118/232" target="_blank" rel="noopener">anthropology</a> to identify and possibly explain 'something different from us', either as individuals or as a society. In these contexts there is a related concept: <b><i>otherness</i></b>.</div>
