    <WithIcon icon="warning" position="right">
        <div slot="html"><b><i>Audience</i></b> is indeed countable but it is a 'collective' noun, and so an (=1) audience can comprise many people.</div>
    </WithIcon>

    <p>If you want to focus on a subgroup of an audience then it is common to refer to these people as 'members of an audience':</p>

    <div class="alert alert-success font-italic">Several <span class="font-weight-bold text-success">members of the audience</span> left before the film finished.</div>

    <p>An example of <b><i>audiences</i></b> (plural) might be:</p>

    <div class="alert alert-success font-italic">The opening of the new James Bond film was enjoyed by <span class="font-weight-bold text-success">audiences</span> up and down the country.</div>

    <p>In this case the same film was watched simultaneously by many different groups of people - <b><i>audiences</i></b> - in many different locations.</p>

