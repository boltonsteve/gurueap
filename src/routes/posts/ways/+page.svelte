    <h1>Ways that have to be done</h1>

    <div class="alert alert-danger font-italic">There are more <span class="font-weight-bold text-danger">ways</span> that have to be <span class="font-weight-bold text-danger">done</span> to halt the spread of HIV.</div>

    <p>'Ways' followed by 'to + V1' is quite common, as in the idiom <b><i>There's more than one way to kill a cat.</i></b></p>

    <WithIcon icon="flag">
        <div slot="html">However, <b><i>way</i></b> (noun) does not collocate in English with <b><i>do</i></b> (verb). You cannot <b><i>do</i></b> a <b><i>way</i></b>. This is possible in Bahasa Indonesia but not in English.</div>
    </WithIcon>

    Another problem here is the redundant use of <b><i>there are</i></b> (see <a href="../posts/there-is-ada" target="_blank">separate post</a>).

    <p>In English you might write:</p>

    <div class="alert alert-success font-italic">More <span class="font-weight-bold text-success">action</span> needs to be <span class="font-weight-bold text-success">taken</span> to halt the spread of HIV.</div>

    <p>Or..</p>

    <div class="alert alert-success font-italic">More <span class="font-weight-bold text-success">solutions</span> needs to be <span class="font-weight-bold text-success">considered</span> to halt the spread of HIV.</div>

    <p>Remember that strong collocation like this will get you a higher score for vocabulary in IELTS speaking and writing. You will find references to collocation in the <a href="../posts/ielts-descriptors" target="_blank">IELTS public band descriptors</a>.</p>

