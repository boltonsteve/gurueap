    <div class="alert alert-danger font-italic"><span class="font-weight-bold text-danger">As we know that</span>, last year the government removed English from the elementary school curriculum.</div>

    <p>English, even academic English, is full of <b><i>fixed expressions</i></b> – phrases that are always written and spoken in exactly the same form.</p>

    <p><b><i>Fixed expressions</i></b> can be quite long and may include some sophisticated grammar, but it’s best to think of them as individual vocabulary items. <i>Record them</i> as vocabulary items. <i>Memorise them</i> as vocabulary items. Don’t change the word order of a fixed expression, and don’t change any word forms inside a fixed expression, even if you think your alterations make sense:</p>

    <ul class="font-italic">
        <li class="font-weight-bold text-success">You’re playing with fire!</li>
        <li>You’re playing with <span class="font-weight-bold text-danger">fires</span>!<br><span class="explanation">(Altered word form)</span></li>
        <li>You’re playing with <span class="font-weight-bold text-danger">flames</span>!<br><span class="explanation">(Changed word)</span></li>
        <li>You’re playing with <span class="font-weight-bold text-danger">the</span> fire!<br><span class="explanation">(Added word)</span></li>
        <li>You’re <span class="font-weight-bold text-danger">with fire playing</span>!<br><span class="explanation">(Changed word order)</span></li>
    </ul>

    <p class="mb-0">You will be less likely to make errors like these if you memorise fixed expressions much as you might memorise individual vocabulary items. You may also notice how the structure of a fixed expression differs from its translation.</p>

    <WithIcon icon="flag" position="right" msg="Indonesians feel a strong urge to add <b><i>bahwa</i></b> after <b><i>seperti kita ketahui</i></b>, whereas in English there is no <b><i>that</i></b>." />

