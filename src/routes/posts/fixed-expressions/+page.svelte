    <p>Notice also that in this example <b><i>as we know</i></b> also requires a comma (,) to separate it from <b><i>last year</i></b>.</p>

    <p>Record fixed expressions in your vocabulary notebook. <i>Review them</i>. <i>Memorise them</i>. <i>Use them</i> in sentences. And watch how your IELTS scores for writing and speaking start to increase!</p>

