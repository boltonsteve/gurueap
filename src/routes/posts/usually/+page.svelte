    <h1>When <i>usually</i> is <i>unusual</i></h1>

    <p class="font-italic ml-3">In my spare time I <span class="font-weight-bold text-danger">usually</span> go out with my friends.</p>

    <p>This is grammatically correct. However, if you're talking about something you do <i>regularly</i> or <i>habitually</i> then <span class="uli">present simple tense</span> is all you need:</p>

<p class="font-italic ml-3">In my spare time I <span class="font-weight-bold text-success">go out with</span> my friends.</p>

<p>{@html flagIndo} Indonesians will feel a need to translate <b><i>biasanya</i></b>, but present simple tense already carries the meaning of <i><b>usually</b></i>, and so <i><b>usually</b></i> is redundant in a sentence like this.</p>

<p>Only use <i><b>usually</b></i> when you want to make a contrast between something you do habitually, and something <b><i>unusual</i></b> that you have to do because of exceptional or <b><i>unusual</i></b> circumstances:</p>

<p class="font-italic ml-3 mb-1">In my spare time I <span class="font-weight-bold text-success">usually go out with</span> my friends, but today I have my IELTS interview.</p>
<p class="mt-0 small ml-3">(You don't <b><i>usually</i></b> have an IELTS interview..)</p>

