    <h1>Budget</h1>

    <div class="alert alert-danger font-italic">In Australia I will <span class="font-weight-bold text-danger">need a lot of budget</span> because I have to buy many things.</div>

    <p>First of all <b><i>budget</i></b> is countable, and since you've written 'a lot of', then there should be an 's' on budget. But there's another problem. <b><i>Budget</i></b> has 2 meanings that are potentially useful in this situation:</p>

    <h3>1. Budget - Sum of money</h3>

    <p>Budget as a sum of money that has been set aside for a particular purpose:</p>

    <div class="alert alert-success font-italic">In Australia I will <span class="font-weight-bold text-success">need a large budget</span> because I have to buy many things.</div>

    <h3>2. Budget - Document</h3>

    <p>Budget as a document containing a list of items:</p>

    <div class="alert alert-success font-italic">In Australia there will be many <span class="font-weight-bold text-success">items on my budget</span>, and so I will need a lot of money.</div>

    <p>In both of these examples..</p>

    <ul class="lh-15">
        <li><i><b>budget</b></i> is a singular countable noun</li>
        <li>'large' collocates with <i><b>budget</b></i></li>
        <li>'item(s)' collocates with <i><b>budget </b></i>(don't forget the preposition 'on')</li>
        <li>as a singular countable noun, <i><b>budget</b></i> requires some kind of determiner (article, possessive, etc.)</li>
    </ul>

    <p>Often students who are preparing for IELTS feel they have to do everything to avoid repetition, and so they use <b><i>budget</i></b> instead of <b><i>money</i></b>. However, <b><i>budget</i></b> is <span class="uli">not</span> synonymous with <b><i>money</i></b>. When you're talking about money and comparing how much things cost, it's safer to use words like <b><i>money</i></b>, <b><i>cheap</i></b>, <b><i>expensive</i></b>, etc.</p>

