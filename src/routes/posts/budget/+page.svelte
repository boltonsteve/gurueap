<blockquote>:( In Australia I will need a lot of budget because I have to buy many things.</blockquote>
First of all <em><strong>budget</strong></em> is countable, and since you've written 'a lot of', then there should be an 's' on <em><strong>budget</strong></em>. But there's another problem. <em><strong>Budget</strong></em> has 2 meanings that are potentially useful in this situation:

1. <em><strong>Budget</strong></em> as <em>a sum of money</em> that has been set aside for a particular purpose:
<p style="padding-left:30px;">:) In Australia I will need <em><strong>a large budget</strong></em> because I have to buy many things.</p>
2. <em><strong>Budget</strong></em> as a document containing <em><strong>a list of items</strong></em>:
<p style="padding-left:30px;">:) In Australia there will be <em><strong>many items on my budget</strong></em>, and so I will need a lot of money.</p>
In both of these examples..
<ul style="padding-left:30px;">
	<li><em><strong>budget</strong></em> is a singular countable noun</li>
	<li>'large' collocates with <em><strong>budget</strong></em></li>
	<li>'item(s)' collocates with <em><strong>budget </strong></em>(don't forget the preposition 'on')</li>
	<li>as a singular countable noun, <em><strong>budget</strong></em> requires some kind of determiner (article, possessive, etc.)</li>
</ul>
Often students who are preparing for IELTS feel they have to do everything to avoid repetition, and so they use <em><strong>budget</strong></em> instead of 'money'. However, <em><strong>budget</strong></em> is <span style="text-decoration:underline;">not</span> synonymous with 'money'. When you're talking about money and comparing how much things cost, it's safer to use words like 'money', 'cheap', 'expensive', etc.

