    <h1>Task 2 Introductions - Sentence #1</h1>

    <div class="alert alert-danger font-italic">In recent years, social media platforms like Instagram, Facebook, and Twitter <span class="font-weight-bold text-danger">have become widely discussed in society</span>.</div>

    <p>Let's analyse this and see if it ticks the right boxes in terms of the desirable features of an opening sentence in IELTS Task 2 writing.</p>

<ol>
 	<li>It tells the reader the topic of the essay contained in the question paper.</li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-ok correct"></span> Well, assuming that the essay is about social media, here we get some examples of <em>social media platforms</em>. So far so good!</li>
</ol>
<ol start="2">
 	<li>It refers to a current aspect of the topic.</li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-ok correct"></span> The time expression <em>'in recent years'</em> suggests that we're going to read about some recent issue relating to social media. We're doing well.</li>
</ol>
<ol start="3">
 	<li>It says something about the topic that is interesting enough to make our reader want to read the rest of the essay.</li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-remove dodgy"></span> I don't think our reader will find this interesting. The writer doesn't say anything interesting about this recent issue, except that it <em>"..has become widely discussed in society."</em> The problem here is that just about any topic can be said to be <strong>widely discussed</strong>. This is not <em>specific</em> enough!</li>

</ol>

    <p>To satisfy criterion 3 (above), we need to say something interesting (and therefore unique) about this topic. For example:</p>

<ul>
 	<li><em>In recent years, social media platforms like Instagram, Facebook, and Twitter <strong><span class="correct">have become alternative sources of news for many people.</span></strong></em></li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-blackboard"></span> This will be interesting for the reader. It will make the reader consider his own <em>use of social media for news access</em>.</li>
 	<li><em>In recent years, social media platforms like Instagram, Facebook, and Twitter <strong><span class="correct">have become increasingly popular, especially among younger people.</span></strong></em></li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-blackboard"></span> This will also be interesting for the reader. The reader will think:Â <em>"Hmm. It may be popular among younger people, but do I like it?"</em></li>
 	<li><em>In recent years, social media platforms like Instagram, Facebook, and Twitter <strong><span class="correct">have given us new and interesting ways to communicate with each other.</span></strong></em></li>
 	<li class="li-explanation"><span class="glyphicon glyphicon-blackboard"></span> Interesting. The reader consider these <em>new and interesting ways to communicate</em>, including <em>the ability to communicate news</em>.</li>
</ul>
When writing opening sentences in Task 2 introductions, try to imagine what might be interesting to the reader. If this topic is indeed <em>widely discussed</em>, then say why it is <em>widely discussed</em>!

