    <h1>Raise or Rise</h1>

    <div class="alert alert-danger font-italic">The percentage of Australians holding a maths, science, or computing degree <span class="font-weight-bold text-danger">raised</span> quite significantly from 10 to 18.</div>

    <p>This is an easy mistake to make. There are 2 verbs with similar meanings. One is transitive (must have an object), the other intransitive (no object).</p>

    <img src="/img/titanic.jpg" class="mx-auto rounded w-50" alt="titanic">
    <p class="small mt-1 text-center">The Titanic</p>

    <p>In your example you use <a href="https://skell.sketchengine.eu/#result?lpos=&query=raise&lang=en&f=concordance" target="_blank" rel="noopener">raise</a>', as in <b><i>raise the titanic</i></b> (V2: raised). However, you have no object, and so what you need is <a href="https://skell.sketchengine.eu/#result?lpos=&query=rise&lang=en&f=concordance" target="_blank" rel="noopener">rise</a> (V2: rose):</p>

    <div class="alert alert-success font-italic">The percentage of Australians holding a maths, science, or computing degree <span class="font-weight-bold text-success">rose</span> quite significantly from 10 to 18.</div>

    <p>Let me try to make a sentence using <b><i>raise</i></b>:</p>

    <div class="alert alert-success font-italic">A combination of improved teaching methods, widespread availability of courses, student interest and governmental commitment <span class="font-weight-bold text-success">raised</span> the percentage of Australians holding a maths, science, or computing degree quite significantly from 10 to 18.</div>

    <p>Possibly you were looking for a synonym for <b><i>increase</i></b> in the hope of avoiding repetition. OK, well the synonym is <b><i>rise</i></b>. But <b><i>increase</i></b> is a very effective word in Task 1 writing. To avoid repetition, use <b><i>increase</i></b> both as a verb and as a noun:</p>

    <div class="alert alert-success font-italic">
        <p class="mt-0">The percentage of Australians holding technical degrees <span class="font-weight-bold text-success">increased</span>.</p>
        <p class="mb-0">At the same time there was <span class="font-weight-bold text-success">an increase in</span> the percentage of women joining the Australian workforce.</p>
