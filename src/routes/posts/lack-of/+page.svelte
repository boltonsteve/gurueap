    <div class="alert alert-danger font-italic">Indonesian students sometimes struggle because they <span class="font-weight-bold text-danger">lack of</span> critical thinking skills.</div>

    <p>It's true that <b><i>lack</i></b> collocates strongly with <b><i>of</i></b>, but only when <b><i>lack</i></b> is a noun. When <b><i>lack</i></b> is a verb, there is no <b><i>of</i></b>, as in the following example:</p>

    <div class="alert alert-success font-italic">Indonesian students sometimes struggle because they <span class="font-weight-bold text-success">lack</span> critical thinking skills.</div>

    <p>If you really want to use <b><i>lack of</i></b> then treat <b><i>lack</i></b> as a noun:</p>

    <div class="alert alert-success font-italic">A <span class="font-weight-bold text-success">lack of</span> critical thinking skills sometimes causes Indonesian students to struggle.</div>

    <p>Once again, in 'lack of', <b><i>lack</i></b> is a noun, <span class="uli">not</span> a verb!</p>

