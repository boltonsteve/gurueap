    <h1><b><i>Could</i></b> <span class="uli">or</span> <b><i>Was able to?</i></b></h1>
    
    <div class="alert alert-danger font-italic">Budi tried to teach himself IELTS but made no progress. Then he discovered GuruEAP and last Saturday he <span class="font-weight-bold text-danger">could</span> achieve band 7.0.</div>

    <p>Ok, I made this one up. It may look like self-promotion, but it’s a problem I often see in student writing.</p>

    <p>Consider this scenario:</p>

    <div class="alert alert-success font-italic">When Steve first arrived in Indonesia he <span class="font-weight-bold text-success">could</span> speak only English and French. Now, after 30 years in Indonesia, he can speak Indonesian fluently. Last weekend he bought some bananas from the local market and he <span class="font-weight-bold text-success">was able to</span> negotiate a reasonable price.</div>

    <p>Here there are two kinds of ability:</p>

    <ol>
        <li class="mb-1">A <i>permanent ability</i> that existed/exists continuously over time (“..he could speak../..he can speak..”). Note that this can be past <i>or</i> present.</li>
        <li>A <i>temporary ability</i> in the past that existed momentarily, relating to a particular event (“..he was able to negotiate..”). Note that this is always past.</li>
    </ol>

    <p>So if we return to the original problem:</p>

    <div class="alert alert-success font-italic">Budi tried to teach himself IELTS but made no progress. Then he discovered GuruEAP and last Saturday <span class="font-weight-bold text-success">he was able</span> to achieve band 7.0.</div>

    <p>Note that the temporary ability was required in a situation that was difficult and required effort / struggle.</p>
    
