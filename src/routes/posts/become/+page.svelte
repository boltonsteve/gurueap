    <h1>When 'become' is not becoming</h1>
    <p class="ml-3 font-italic">Full-day school <span class="text-danger font-weight-bold">becomes</span> an important issue because it concerns a wide range of people, especially parents.</p>
    <p>This is the influence of Bahasa Indonesia. In English 'become' is always used to describe a change, rather than a constant:</p>
    <ul class="font-italic lh-15">
        <li>People <span class="font-weight-bold text-success">become</span> sleepy when they drink a lot of beer.</li>
        <li>Most knives <span class="font-weight-bold text-success">become</span> dull after a while and need to be sharpened.</li>
        <li>When there's a problem, Clark Kent <span class="font-weight-bold text-success">becomes</span> Superman.</li>
    </ul>
    <p>In each of these three cases, a <b><i>change</i></b> is implied:</p>
    <ul class="lh-15 lst-none">
        <li>..from alert to sleepy</li>
        <li>..from sharp to dull</li>
        <li>..from newspaper journalist to superhero</li>
    </ul>

    <p>These are all familiar, recurring situations, and so we use present simple tense to describe them.</p>

    <p>If we say "Full-day school becomes an important issue," a <b><i>change</i></b> is indeed implied (from non full-day school to full-day school), but since this is a unique, rather than a recurring situation, then we need a time frame.</p>

    <p>If the <b><i>change</i></b> happened in the past, but we're not sure exactly when, then we use present perfect tense:</p>
    <p class="ml-3 font-italic">Full-day school <span class="font-weight-bold text-success">has become</span> an important issue.</p>
    <p>If the <b><i>change</i></b> is happening right now - continuously - then we can use present continuous tense:</p>
    <p class="ml-3 font-italic">Full-day school <span class="font-weight-bold text-success">is becoming</span> an important issue.</p>
    <p>However, if we are analysing a situation that is true now, constant and <b><i>without change</i></b>, as though we are looking at it under a microscope, then we use present simple tense:</p>
    <p class="ml-3 font-italic">Full-day school <span class="font-weight-bold text-success">is</span> an important issue.</p>
