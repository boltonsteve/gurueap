    <h1>Idiomatic language</h1>

    <p>The <a href="../posts/ielts-descriptors" target="_blank">IELTS public band descriptors for speaking</a> state that in order to achieve band 7 or above for Lexical Resource (LR - vocabulary), then you should use some idiomatic language.</p>

    <h3 class="mb-0">Idioms and collocation</h3>
    <p class="mt-0"><b><i>Idiomatic language</i></b> gets you a high score for vocabulary because idiomatic phrases contain <b><i>strong collocation</i></b>. Take for example:</p>

    <p class="font-italic ml-3">I eat nasi padang <b><i>once in a blue moon</i></b>.</p>

    <p>Like any other idiom, the words - <b><i>once in a blue moon</i></b> - always appear in <span class="uli">exactly the same form</span> and in <span class="uli">exactly the same order</span>. The words <a href="../posts/collocation" target="_blank">collocate strongly</a> with each other.</p>

    <p>We <span class="uli">never</span> say:</p>

    <ul class="font-italic text-danger">
        <li><b>twice</b> in a blue moon</li>
        <li>once <b>on</b> a blue moon</li>
        <li><b>blue once on a</b> moon</li>
    </ul>

    <h3 class="mb-0">Use idioms <i><b>appropriately</b></i></h3>
    <p class="mt-0">There is a lot of teaching material available online dealing with idioms, but there are very few opportunities to use the commonly taught idioms in IELTS speaking and writing.</p>

    <p>A blue moon occurs once every 2.5 years, so if you eat nasi padang no more than once every two years, then ok. Otherwise, if you eat nasi padang once every few months, then saying <b><i>once in a blue moon</i></b> would <span class="uli">not be appropriate</span>.</p>

    <h3 class="mb-0">Say what you mean</h3>
    <p class="mt-0">If you use <span class="font-weight-bold text-primary">an idiom</span>, it's a good idea to <span class="font-weight-bold text-success">explain what you mean</span>. For example, if you use <b><i>think outside the box</i></b>, do it like this:</p>

    <p class="font-italic ml-3">My colleague Budi likes to <span class="font-weight-bold text-primary">think outside the box</span>. <span class="font-weight-bold text-success">He always has many new and creative ideas</span>.</p>

    <p>If you cannot do this, then you probably don't know what the idiom means and you probably shouldn't be using it.</p>

