    <p>Let’s look at what we are trying to communicate in this sentence:</p>

    <p class="text-center font-weight-bold">[…thing A…] <i>is the key to</i> [..thing B..].</p>

    <p>OK. So <b><i>thing A</i></b> is obviously a thing or a behaviour that will help us to achieve <b><i>thing B</i></b>, which is a thing or a behaviour that is difficult to achieve without <b><i>thing A</i></b>. If <b><i>thing B</i></b> is a thing, then it is a noun and <span class="uli">not</span> a verb, so we need the noun form of 'grasp', which is 'grasp<b><i>ing</i></b>':</p>

    <div class="alert alert-success font-italic">Conserving a language is the key <span class="font-weight-bold text-success">to</span> fully <span class="font-weight-bold text-success">grasping</span> the identity of a culture.</div>

    <p>For some reason the expressions <b><i>the key to</i></b> and <b><i>the answer to</i></b> often lead to this error, so watch out for these! In this structure, <b><i>to</i></b> is a <i>preposition</i> and <span class="uli">not</span> part of the infinitive <b><i>to + V1</i></b>.</p>

