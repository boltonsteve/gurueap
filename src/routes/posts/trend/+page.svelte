    <WithIcon icon="warning" position="right" msg="When students are preparing for IELTS Task 1 writing they learn the importance of describing <b><i>trends</i></b> in graphs, tables and charts. Not surprisingly, they go ahead and use the word <b><i>trend</i></b> to describe these trends. But native speakers are less likely to do this." />

    <p class="mt-0">In the opening example a trend is described, but the word <b><i>trend</i></b> is redundant. It is enough to write:</p>

    <div class="alert alert-success font-italic">In general, all the lines show that the incidence of bronchitis is increasing.</div>

    <p>This kind of statement stands out as a <b><i>trend</i></b> because it says something general about a change over time without mentioning data.</p>

    <p>So here’s your checklist for a trend:</p>

    <ul>
        <li>It is expressed as a sentence</li>
        <li>It does not feature the word ‘trend’</li>
        <li>It says something general without mentioning values from the graph, table or chart</li>
        <li>It describes a change over time</li>
        <li>The thing that is affected by change is named specifically (‘incidence’).</li>
    </ul>

    <p class="mb-0">Understand also, that a trend is usually apparent in data that is highly irregular. In the following graph grammatical accuracy goes up and down erratically over time, but the general trend (shown by the straight line) is downward.</p>

