    <h1>Collocation</h1>

    <p>To get a good score in IELTS for Lexical Resource (LR), which is IELTS speak for <b><i>vocabulary</i></b>, you need to demonstrate an awareness of style and <b><i>collocation</i></b>. At least that's what it says in the <a href="../posts/ielts-descriptors" target="_blank">LR descriptor at Band 7</a>.</p>

    <p class="font-weight-bold text-center">Co (together) + location (location!) = Collocation</p>

    <p>Sometimes - in fact quite often - 2 or more words appear together, but not for any grammatical reason. They just do!</p>

    <p>For example, which of the following do we <span class="uli">never</span> say?</p>

    <ul>
        <li>Merry Christmas</li>
        <li>Happy Birthday</li>
        <li>Merry Birthday</li>
    </ul>

    <p>We <span class="uli">never</span> say <b><i>Merry Birthday</i></b>. It's grammatically correct, and it's communicative, but we never say it.</p>

    <p>Another example would be <span class="font-weight-bold text-primary">verbs</span> that go together with <span class="font-weight-bold text-success">nouns</span>. Here is an example from Bahasa Indonesia.</p>

    <WithIcon icon="flag">
        <div slot="html">I have to <span class="font-weight-bold text-primary">make</span> my <span class="font-weight-bold text-success">homework</span>.</div>
    </WithIcon>

    <p>In Bahasa Indonesia, the <span class="font-weight-bold text-primary">verb</span> <b><i>make</i></b> collocates strongly with the <span class="font-weight-bold text-success">noun</span> <b><i>homework</i></b>.</p>

    <p>In English the verb-noun <b><i>collocation</i></b> is different:</p>

    <WithIcon icon="flag" flag="uk">
        <div slot="html">I have to <span class="font-weight-bold text-primary">do</span> my <span class="font-weight-bold text-success">homework</span>.</div>
    </WithIcon>

    <p>Low band IELTS candidates tend to translate directly to English from their first language, and often the result is <b><i>weak collocation</i></b>.</p>

    <p>High band candidates tend to <i>think in English</i>, and as a result they have in their minds <b><i>chunks</i></b> of language that include <b><i>strong collocation</i></b>.</p>

    <p>For example, in the sentence..</p>

    <p class="font-italic ml-3">But some, such as pregnant people and those with weakened immune systems, are at greater risk of getting sick from animals.</p>

    <p>..there are at least 6 <b><i>chunks</i></b>:</p>

    <ul class="font-italic">
        <li>But some</li>
        <li class="lst-none small">synonym for 'but some people'</li>
        <li>such as</li>
        <li class="lst-none small">collocation - synonym for 'for example'</li>
        <li>pregnant people</li>
        <li>and those with weakened immune systems</li>
        <li>are at greater risk of</li>
        <li class="lst-none small">collocation 'at (greater) risk of'</li>
        <li>getting sick from animals</li>
        <li class="lst-none small">collocation 'get sick from'</li>
    </ul>

    <p>Mostly these <b><i>chunks</i></b> are acquired through reading. Indeed, regular reading is the best way to acquire <b><i>strong collocation</i></b>.</p>

    <p class="font-weight-bold">Go do some reading!</p>
    

