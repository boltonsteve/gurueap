    <h1>Labouring over <b><i>labourers</i></b></h1>

    <p class="font-italic alert alert-danger">Some people claim that working hours for <span class="font-weight-bold text-danger">labours</span> in factories are too long.</p>

    <WithIcon icon="flag">
        <div slot="html">Here an Indonesian IELTS candidate is trying to find a synonym for <b><i>worker</i></b>, but has ended up using the wrong word form, and possibly even an inappropriate word.</div>
    </WithIcon>

    <p>In English a <b><i>labourer</i></b> ('labour' + 'er') does work that distinguishes him or her from other kinds of worker:</p>

<ul>
	<li>Labourers are usually unskilled.</li>
	<li>Labourers often have to use physical strength because their work requires them to lift and carry things.</li>
	<li>The work of labourers is generally outdoor work.</li>
	<li>Labouring is often dirty work.</li>
	<li>Labouring is not very well paid in most countries.</li>
</ul>


    <div style="width:180px;float:right;" >
