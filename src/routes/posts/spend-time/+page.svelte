    <h1>Spend time</h1>

    <p class="font-italic ml-3">At the weekend I like to spend <span class="font-weight-bold text-danger">my</span> time <span class="font-weight-bold text-danger">to cook</span>.</p>

    <p>There are two problems here. First of all you are unlikely to spend anybody else's time other than your own, so <b><i>my</i></b> is redundant. Secondly, <b><i>spend time</i></b> is much more commonly followed by <b><i>_ing</i></b>:</p>

    <p class="font-italic ml-3">At the weekend I like to <span class="font-weight-bold text-success">spend time cooking</span>.</p>

    <p>You can also use a <b><i>preposition phrase</i></b> to show where you spend time:</p>

    <p class="font-italic ml-3">At the weekend I like to spend time <b><i>in the kitchen</i></b>.</p>

    <p>And you can even combine these two examples:</p>

    <p class="font-italic ml-3">At the weekend I like to <span class="font-weight-bold text-success">spend time</span> <b><i>in the kitchen</i></b> <span class="font-weight-bold text-success">cooking</span>.</p>

    <p>Now spend some time practicing <b><i>spend time</i></b>!</p>

</div>
