    <h1>'Like with like' in Task 1</h1>

    <div class="alert alert-danger font-italic">The population of Japan is <span class="font-weight-bold text-danger">lower than Thailand</span>.</div>

    <p>This is an example of <span class="uli">not</span> comparing <b><i>like with like</i></b>.</p>

    <p>In the noun phrase 'the population of Japan', <b><i>population</i></b> is the main noun. <b><i>Population</i></b>, which is a <span class="uli">mass of people</span>, is said here to be lower than Thailand, which is a <span class="uli">land mass</span>.</p>

    <WithIcon icon="warning" msg="This leaves the reader with an image of Thailand hovering up in the air, with the Japanese population some physical distance below it!" />

    <p>A mass of people is not <i>like</i> a land mass. In order to make sure that you're comparing <b><i>like with like</i></b>, use a parallel structure:</p>

    <div class="alert alert-success font-italic"><b><i>The <span class="uli">population</span> of Japan</i></b> is lower than <b><i>the <span class="uli">population</span> of Thailand</i></b>.</div>

    <p>This may result in some repetition - 'the population of' is used twice. But don't worry about repetition. Just make sure that you're comparing <b><i>like with like</i></b>.</p>

    <h3>Avoiding repetition - uncountable and singular nouns</h3>

    <p>If you want to avoid repeating a noun that is either uncountable or singular, you can use <b><i>that</i></b> as a substitute:</p>

    <div class="alert alert-success font-italic"><b><i>The <span class="uli">population</span> of Japan</i></b> is lower than <b><i><span class="uli">that</span> of Thailand</i></b>.</div>

    <p>In this example, <b><i>that</i></b> replaces <b><i>the population</i></b>.</p>

    <h3>Avoiding repetition - plural nouns</h3>

    <p>If you want to avoid repeating a noun that is plural countable, you can use <b><i>those</i></b> as a substitute:</p>

    <div class="alert alert-success font-italic"><b><i><span class="uli">Prices</span> at traditional markets</i></b> are lower than <b><i><span class="uli">those</span> at supermarkets</i></b>.</div>

    <p>In this example, <b><i>those</i></b> replaces <b><i>prices</i></b>.</p>

