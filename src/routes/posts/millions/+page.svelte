    <h1>Million or millions?</h1>

    <div class="alert alert-danger font-italic">According to a recent census, there are 265 <span class="font-weight-bold text-danger">millions</span> <b>people</b> living in Indonesia.</div>

    <p>Yes, I know it seems right. But it isn't.</p>

    <h3 class="mb-0">When <i>million</i> is the main noun</h3>
    <p class="mt-0">Only put an 's' on <b><i>million</i></b> when <b><i>million</i></b> is the main noun in a noun phrase, for example when it is at the beginning of a sentence, like this:</p>

    <div class="alert alert-success font-italic"><span class="font-weight-bold text-success">Millions</span> <b>of people</b> live in Indonesia, a huge archipelago in south-east Asia.</div>

    <p>..or inside a sentence like this:</p>

    <div class="alert alert-success font-italic">Indonesia spends <span class="font-weight-bold text-success">millions</span> <b>of dollars</b> every year subsidising fuel.</div>

    <p>In the noun phrase <i>millions of people</i>, <b><i>millions</i></b> is the main noun, modified by <b><i>of people</i></b>. We know exactly which <b><i>millions</i></b> you're talking about - not millions of bananas, for example! The same goes for <i>millions of dollars</i> (<span class="uli">not</span> <i>millions of rupiah</i>!).</p>

    <h3 class="mb-0">When <i>million</i> is NOT the main noun</h3>
    <p class="mt-0">When <b><i>million</i></b> is modifying another noun, don't add an 's':</p>

    <div class="alert alert-success font-italic">According to a recent census, there are 265 <span class="font-weight-bold text-success">million</span> people living in Indonesia.</div>

    <p>In the noun phrase <i>265 million people living in Indonesia</i>, the main noun is <b><i>people</i></b>. All of the other words in the phrase give us information about <b><i>people</i></b> - how many, and where they live.</p>

    <p>The same rule applies to hundred(s), thousand(s), etc.</p>

