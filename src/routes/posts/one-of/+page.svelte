    <h1>One of</h1>

    <div class="alert alert-danger font-italic">Euthanasia may be <span class="font-weight-bold text-danger">one of</span> ways to deliver health resources fairly to people who still want to live. </div>

    <p>This is a direct translation from Bahasa Indonesia: <b><i>salah satu</i></b>.</p>

    <p>For every noun in English it is important to communicate one of three meanings:</p>

<ul>
    <li>One of many</li>
    <li>All of them everywhere</li>
    <li>This one exactly</li>
</ul>

<p>In the opening example the writer communicated successfully. However, the sentence is gramatically incorrect. If you really must use <b><i>one of</i></b> then you need more grammar:</p>

<div class="alert alert-success font-italic">Euthanasia may be one of several ways to deliver..<br><span class="small">('several' behaves like 'many')</span></div>

<div class="alert alert-success font-italic">Euthanasia may be one of the best ways to deliver..<br><span class="small">('one of' + the + superlative adjective + plural count noun)</span></div>

<p>You can see how easy it can be to introduce grammatical error, or to choose the wrong word to follow <b><i>one of</i></b>! A much easier and far more common way to communicate <i>one of many</i> is using the indefinite article <b><i>a</i></b> (or <b><i>an</i></b>) - <span class="uli">NOT</span> <b><i>one of</i></b>.</p>

<p>Want to communicate <b><i>one of many</i></b>? Use the <i>indefinite article</i>:</p>

<div class="alert alert-success font-italic">Euthanasia may be <span class="font-weight-bold text-success">a</span> way to deliver health resources fairly to people who still want to live.</div>

