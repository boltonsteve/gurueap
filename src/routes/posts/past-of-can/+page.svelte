    <h1>The past of <b><i>can</i></b></h1>

    <div class="alert alert-danger font-italic">Budi tried to teach himself IELTS but made no progress. Then he discovered GuruEAP and last Saturday he <span class="font-weight-bold text-danger">could</span> achieve band 7.0.</div>

    <p>Ok,ok.. I made this one up. It may look like shameless self-promotion, but it's a problem I often see in student writing. Honest.</p>

    <p>Consider this scenario:</p>

    <p class="ml-3 font-italic">When GuruEAP first arrived in Indonesia he <b>could</b> speak only English and French. Now, after 27 years in Indonesia, he <b>can</b> speak Indonesian fluently. Last weekend he bought some bananas from the local market and he <b>was able to</b> negotiate a reasonable price.</p>

    <p>Here there are two kinds of ability:</p>

<ol class="lh-15">
	<li>A permanent ability that existed/exists <span class="uli">continuously</span> over time ("..he <b><i>could</i></b> speak../..he <b><i>can</i></b> speak.."). Note that this can be past or present.</li>
	<li>A temporary ability in the past that existed <span class="uli">momentarily</span>, relating to a particular event ("..he <b><i>was able to</i></b> negotiate.."). Note that this is <span class="uli">always past</span>.</li>
</ol>

<p>So if we return to the original problem:</p>

<div class="alert alert-success font-italic">Budi tried to teach himself IELTS but made no progress. Then he discovered GuruEAP and last Saturday he <span class="font-weight-bold text-success">was able to</span> achieve band 7.0.</div>

<WithIcon icon="warning" position="right">
