    <h1>When risk is risky</h1>

    <div class="alert alert-danger font-italic">40% of people living with HIV have <span class="font-weight-bold text-danger">risk</span> to develop tuberculosis.</div>

    <p><b><i>Risk</i></b> can be a <b><i>risky</i></b> word in English!</p>

    <p>We can assume that if there's a <b><i>risk</i></b>, then there is some kind of <b><i>bad thing</i></b> causing the <b><i>risk</i></b>, as in the example above - 'developing tuberculosis'.</p>

    <h3 class="mb-0">Risk - verb</h3>
    <p class="mt-0">Here there are basically three options.</p>

<div class="alert alert-success mb-2">
    <p class="font-italic mt-0">40% of people living with HIV <span class="font-weight-bold text-success">are at risk of</span> developing tuberculosis.</p>
<p class="mb-0 small font-weight-bold">subject + to be + at risk of + name of bad thing</p>
</div>

<div class="alert alert-success mb-2">
    <p class="font-italic mt-0">A weak immune system can make it easier to contract tuberculosis, and people living with HIV <span class="font-weight-bold text-success">are</span> <b><i>particularly</i></b> <span class="font-weight-bold text-success">at risk</span>.</p>
<p class="mb-0 small font-weight-bold">clause mentioning the bad thing + subject + to be + (adverb) + at risk</p>
</div>

<div class="alert alert-success">
    <p class="font-italic mt-0">40% of people living with HIV <span class="font-weight-bold text-success">risk</span> <b><i>develop</i></b><span class="font-weight-bold text-success">ing</span> tuberculosis.</p>
<p class="mb-0 small font-weight-bold">subject + risk + name of bad thing</p>
</div>

<h3 class="mb-0">Risk - noun</h3>
<p class="mt-0">Here the 'bad thing' is not mentioned.</p>

<div class="alert alert-success mb-2">
    <p class="font-italic mt-0">People who have unprotected sex are taking a risk.</p>
    <p class="small font-weight-bold mb-0">subject + take + a risk/risks</p>
    <p class="small mb-0">Notice the collocation: <b><i>take a risk!</i></b></p>
</div>

<p>Risk as noun is also often used as a noun modifier:</p>

<div class="alert alert-success">
    <p class="font-italic mt-0">Unprotected sex is a high risk activity.</p>
    <p class="small font-weight-bold mb-0">subject + to be + a + high risk + noun</p>
</div>

<h3>Risky - adjective</h3>
<p>Here again, the 'bad thing' is not mentioned.</p>

<div class="alert alert-success">
    <p class="font-italic mt-0">Unprotected sex is risky.</p>
    <p class="small mb-0 font-weight-bold">subject + to be + risky</p>
</div>


