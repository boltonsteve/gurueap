    <h1>Rather than</h1>

    <div class="alert alert-danger font-italic">Most people currently suffer from anxiety <span class="text-danger font-weight-bold">rather than</span> ancient people in the past.</div>

    <p>Literally this means:</p>
    <ol>
        <li>Modern people suffer from anxiety.</li>
        <li>Modern people do not suffer from ancient people.</li>
    </ol>

    <p>First of all I think our writer would be better off using <b><i>unlike</i></b>:</p>

    <div class="alert alert-success font-italic">Most modern people suffer from anxiety, <span class="text-success font-weight-bold">unlike</span> ancient people in the past.</div>

    <p>Here the meaning is:</p>
    <ol>
        <li>Modern people suffer from anxiety.</li>
        <li>Ancient people in the past did not suffer from anxiety.</li>
        <li>Modern people are not like ancient people.</li>
    </ol>

    <h2>Rather (than) #1</h2>
    <p>You can use <b><i>rather</i></b> in situations in which you express a preference:</p>

    <div class="alert alert-success font-italic">
        <p class="font-italic"><span class="font-weight-bold text-success">Rather than</span> spend 2 hours a day stuck in traffic, I would prefer to stay at home.</p>
        <p class="font-italic">Being stuck in traffic is no fun. I<span class="font-weight-bold text-success">'d rather</span> stay at home and watch a movie on Netflix.</p>
    </div>

    <h2>Rather (than) #2</h2>
    <p>You can also use rather to show that something belongs more to one category and less to another:</p>

    <div class="alert alert-success font-italic">
        <p class="font-italic">This book is journalism <span class="font-weight-bold text-success">rather than</span> science.</p>
        <p class="font-italic">The best security policies are proactive <span class="font-weight-bold text-success">rather than</span> reactive.</p>
        <p class="font-italic">Use body wash <span class="font-weight-bold text-success">rather than</span> bar soap.</p>
    </div>

