    <h1>Famous or Popular</h1>

    <p>My school friend Budi is famous. He always helps people with their homework and he's really good at sport.</p>

    Bahasa Indonesia has a word that is often used to translate both popular and famous - terkenal.

    Let's examine the meanings of famous and popular:

    <h3>Famous</h3>
    <ul>
        <li>
            Well known over a very wide area..
            <ul>
                <li>Nationally</li>
                <li>Perhaps Internationally</li>
            </ul>
        </li>
        <li>Usually seen in national or international news and in the wider media.</li>
        <li>Usually - but not necessarily - well liked.</li>
        <li>Usually - but not necessarily - talented.</li>
    </ul>

    <h3>Popular</h3>
    <ul>
        <li>Well liked by many people, either locally or nationally, or even internationally.</li>
        <li>Possibly - but not necessarily - talented.</li>
    </ul>

