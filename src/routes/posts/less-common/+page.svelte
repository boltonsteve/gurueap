    <h1>Less common vocabulary</h1>

    <p>The IELTS public band descriptors for speaking state that in order to achieve band 7 or above for Lexical Resource (LR - vocabulary), then you should use some <a href="../posts/ielts-descriptors" target="_blank">less common vocabulary</a>.</p>

    <p class="font-italic ml-3">It's so hot today. I'm <b><i>parched!</i></b></p>

    <p>In this sentence, <b><i>parched</i></b> is an example of less common, or low-frequency vocabulary. If you use a word like parched, the IELTS examiner will begin to consider you as potentially band 7 or above.</p>

    <p>Band 7 candidates, even band 8 candidates, might not know <b><i>parched</i></b>. Perhaps it is new to you today?</p>

    <p>An example of a typically band 7 low-frequency word might be <b><i>plummet</i></b>:</p>

    <p class="font-italic ml-3">In recent months, sales have <b><i>plummeted</i></b>.</p>

    <h3 class="mb-0">Word forms and frequency</h3>
    <p class="mt-0">Sometimes one form of a word is frequent, while another form is less frequent.</p>

    <ul>
        <li class="font-italic">I'm still very young, and so <b>domesticity</b> is still a long way away for me!</li>
        <li class="small lst-none mb-1">(less frequent than <b><i>domestic</i></b>)</li>
        <li class="font-italic">My government definitely needs to think harder about <b>inclusivity</b>.</li>
        <li class="small lst-none mb-1">(less frequent than <b><i>inclusive</i></b>, which is less frequent than <b><i>include</i></b>)</li>
        <li class="font-italic">Presidential candidates in my country like to demonstrate their <b>religiosity</b>.</li>
        <li class="small lst-none mb-1">(less frequent than <b><i>religious</i></b>)</li>
    </ul>

    <p>Notice that in each of these examples, the low-frequency word form is the noun form! In a lot of academic writing, the noun form is more useful than other word forms, because it can be strategically located at the beginning of a sentence in what is known as theme position.</p>

    <h3 class="mb-0">Vocabulary from your field</h3>
    <p class="mt-0">If you are scholar preparing for study abroad, then you have probably acquired a large vocabulary of low-frequency items related to your academic specialisation. If the examiner asks questions related to your field, use this vocabulary!</p>

    <p>It would be fair to describe the following as band 9 items:</p>

    <ul>
        <li class="font-italic">I don't really like music that is <b>aleatoric</b>.</li>
        <li class="lst-none small mb-1">(when asked about music you do not like, a specialist term from the field of music)</li>
        <li class="font-italic">I prefer <b>calisthenic</b> training rather than using weights for strength.</li>
        <li class="lst-none small mb-1">(when asked about what kind of exercise you do, a specialist term from the field of health)</li>
        <li class="font-italic">In my work I specialise in <b>cantilevers</b>.</li>
        <li class="lst-none small mb-1">(when asked about the kind of work you do, a specialist term from the field of engineering / architecture)</li>
    </ul>

    <h3 class="mb-0">The importance of reading</h3>
    <p class="mt-0">An awareness of the frequency of vocabulary items comes through reading. When you read a novel, and you see a word often, then that word is a high-frequency word.</p>

