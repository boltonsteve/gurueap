    <h1>Coherence and Cohesion (CC)</h1>

<h2>Cohesion</h2>

<p><b><i>Cohesion</i></b> is the glue that sticks a sentence to another in a paragraph, or a paragraph to another paragraph.</p>

<p>A text can be <b><i>cohesive</i></b> through the use of the following devices:</p>

<h3>Repetition</h3>
<p class="strategy">In sentence B (the second of any two sentences), repeat a word or phrase from sentence A:</p>

<p class="font-italic ml-3"><b><i>Delegates</i></b> from Indonesia, Thailand and <b><i>Malaysia</i></b> have arrived at the conference. The <b><i>delegates</i></b> from <b><i>Malaysia</i></b> are staying at the Hilton.</p>

<h3>Synonymy</h3>
<p class="strategy">If direct repetition is too obvious, or if you are aiming to demonstrate a wide range of vocabulary, use a synonym of the word you wish to repeat.</p>

<p class="font-italic ml-3">Astronauts are again struggling to land on <b><i>the surface of the moon</i></b>. <b><i>The lunar surface</i></b> is covered with rocks and craters.</p>

<div class="alert text-center">
    Only use a synonym if a suitable synonym is available!
</div>

<h3>Antonymy</h3>
<p class="strategy">Using the opposite word, an antonym, can also create sentence cohesion.</p>

<p class="font-italic ml-3">It is expected that there will be a dramatic <b><i>increase</i></b> in sales later this year. A <b><i>decrease</i></b> is extremely unlikely.</p>

<h3>Transition Signals</h3>
<p class="strategy">Sometimes called <b><i>connectives</i></b> or <b><i>discourse markers</i></b>, these are used to communicate cause/effect, comparison, time order, logical relationships, etc.</p>

<p class="font-italic ml-3">Our product is not selling well. <b><i>First of all</i></b> its is expensive. <b><i>In addition</i></b>, the design is outdated.</p>

<h3>Parallelism</h3>
<p class="strategy">Repeat a sentence structure. This technique is the oldest, most overlooked, but probably the most elegant method of creating cohesion.</p>

<p class="mb-0 ml-3 font-italic">If I have an exam, I <b><i>look for</i></b> a quiet place to study, <b><i>switch off</i></b> my phone, and <b><i>leave</i></b> a 'do not disturb' sign on the door.</p>
<p class="small mt-1 ml-3">(parallel clauses..)</p>

<h2>Coherence</h2>

<p><b><i>Coherence</i></b> means that the text is easy to read and understand because:</p>

<ul>
    <li class="mb-1">the organisation of ideas in the text is systematical<br><span class="small">(the text may have a 'generic' structure)</span></li>
    <li>the text is logical<br><span class="small">(the relationships between claims and supports, objections and rebuttals, time order, cause and effect, compare contrast, etc., are clearly marked)</span></li>
</ul>

