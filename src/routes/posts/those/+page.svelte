    <p>Ok let's start with the example given in the picture above. Although they are not mentioned separately, we can see that the writer is implicitly referring to two groups of people:</p>

    <ul>
        <li>people who believe in magic</li>
        <li>people who do not believe in magic</li>
    </ul>

    <p>The same text could have been written as follows:</p>

    <div class="alert alert-success font-italic">Some people believe in magic. Other people do not believe in magic. <span class="font-weight-bold text-success">Those</span> who do not believe in magic will never find it.</div>

    <p>The writer is using <b><i>those</i></b> to focus on one of the groups.</p>

    <p>This is the function of <b><i>those</i></b>.</p>

    <p>If we return to the financial assistance example, the focus is on only one group - <i>people who cannot afford to send their children to school</i>. In which case you need <b><i>these</i></b>:</p>

    <div class="alert alert-success font-italic">Many people in the region cannot afford to send their children to school. <span class="font-weight-bold text-success">These</span> people receive some financial assistance from the government, but it is not enough.</div>

    <p>Whenever you are dealing with more than one group of things or people and you want to focus on one of the groups, use <span class="font-weight-bold text-success">those</span>. Here is another example:</p>

    <div class="alert alert-success font-italic">These days smartphones are produced by a wide range of manufacturers. <span class="font-weight-bold text-success">Those</span> made by Apple are beautifully designed, but tend to be rather expensive.
        <p class="small mb-0">(the writer also has in mind Samsung, Nokia, etc.)</p>
    </div>

    <p>Very often those is followed immediately by a relative pronoun - who, that, which, etc.:</p>

    <ul class="lh-15">
        <li>It makes <b><i>those who</i></b> appoint judges understandably cautious.</li>
        <li><b><i>Those who</i></b> regularly drink milk feel less stressed.</li>
        <li>The greatest ideas are <b><i>those that</i></b> are the simplest.</li>
        <li>Many of <b><i>those that</i></b> remain are very small.</li>
        <li>The only houses with weaker protections are <b><i>those which</i></b> remain empty.</li>
        <li>A special category of ECUs are <b><i>those which</i></b> are programmable.</li>
        <li>The very best dinner parties are <b><i>those where</i></b> you meet new best friends.</li>
        <li>The most boring presentations are <b><i>those where</i></b> the speaker reads the words on the screen.</li>
    </ul>

    <p>Notice that in all of the preceding examples, the relative pronoun is followed by a dependent clause (e.g. 'who appoint judges understandably cautious').</p>

    <p>Be careful with <b><i>those in which</i></b>. This is followed by an independent clause.</p>

    <ul class="lh-15">
        <li>The stories that people most love are <b><i>those in which</i></b> characters overcome unusual limitations.</li>
        <li>Matrifocal societies are <b><i>those in which</i></b> women, especially mothers, occupy a central position.</li>
    </ul>

