    <h1>Contrasting with 'while'</h1>

    <div class="alert alert-danger font-italic">The average years of education of a person without a disability is 6.5 years. <span class="text-danger font-weight-bold">While</span>, for someone with a moderate disability it is 4.4 years.</div>

    <p>Here the writer is basically contrasting people with a disability and people without a disability in terms of the average number of years they spend in education.</p>

    <p>However, to contrast using <b><i>while</i></b>, there are 2 options:</p>

    <ol>
        <li><b><i>While</i></b> clause one, clause two.</li>
        <li>Clause one, <b><i>while</i></b> clause two.</li>
    </ol>

    <p>And so our writer needs to do one of the following:</p>

