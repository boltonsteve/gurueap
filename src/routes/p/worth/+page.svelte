    <h1>Is it worth it?</h1>

    <div class="alert alert-danger font-italic">Is it <span class="font-weight-bold text-danger">worth to spend</span> large amounts of money on space exploration?</div>

    <p>This is an expression that doesn't really have a nice translation in Bahasa Indonesia, and so I seldom hear it from students. But it's extremely common in spoken and written English, and so it's one you should learn to use.</p>

    <p>This is the correct collocation:</p>

    <div class="alert alert-success font-italic">Is it <span class="font-weight-bold text-success">worth spending</span> large amounts of money on space exploration?</div>

    <p>Possible answers include..</p>

    <ul class="font-italic lh-15">
        <li>Yes, it's (it is) worth it.</li>
        <li>Yes, it's (it is) worth spending money on space exploration.</li>
        <li>No, it isn't (it is not) worth it.</li>
        <li>No, it's (it is) not worth it.</li>
        <li>No, it isn't (is not) worth spending money on space exploration.</li>
    </ul>

    <p>When you ask "Is it worth it?" you're asking..</p>

    <ul class="font-italic lh-15">
        <li>Is it basically more advantageous than disadvantageous?</li>
        <li>Is the extra expense justified?</li>
        <li>Is the additional time investment justified?</li>
    </ul>

    <p>And so we have the idiom <b><i>If a job's worth doing, it's worth doing right!</i></b> If you're investing extra time and/or money into a job or task, then it would be a sin to put in less than 100% effort:</p>

    <img src="/img/one-job.jpg" class="rounded mx-auto" alt="one job" style="width:300px;">

    <p>And now for the challenge. Can you think of 5 activities that require additional time, effort and expense but are still worth it?</p>

