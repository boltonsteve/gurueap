    <p>Here there are 2 subjects:</p>

    <ol class="font-italic">
        <li>the divorce rate in America</li>
        <li>the divorce rate in Indonesia</li>
    </ol>

    <p>Some of you will complain about the repetition in this sentence ('the divorce rate'). However, it's better to repeat words and phrases and communicate something meaningful than to avoid repetition and communicate nothing.</p>

    <p>Actually in this example repetition can be avoided:</p>

    <div class="alert alert-success font-italic mb-2"><span class="uli">The divorce rate in America</span> rose steadily, while <span class="uli">that in Indonesia</span> fell dramatically.</div>

