    <h1>Lazy to read</h1>

    <div class="alert alert-danger font-italic">I know that reading is important, but Indonesian people are <span class="font-weight-bold text-danger">lazy to</span> read.</div>

    <WithIcon icon="flag">
        <div slot="html">This is an almost direct translation of <b><i>malas baca</i></b>, right?</div>
    </WithIcon>

    <img src="/img/reading-snoopy.png" alt="reading snoopy" style="width:180px;float:right;">
    <p>In most English speaking countries, people who read are usually educated and interested in the world, and these are seen as positive characteristics. Meanwhile laziness is thought to be a very negative characteristic.</p>

    <p>Nobody in an English speaking country would openly admit to being 'lazy to read' - they would feel too embarrassed.</p>

    <p>In any case 'lazy to + V1' is bad collocation. In a situation when laziness is more appropriate, the native English speaker might say:</p>

    <div class="alert alert-success font-italic">I <span class="font-weight-bold text-success">can't be bothered to</span> go jogging this morning. Anyway it's raining.</div>

    <p>If you are ready to admit your laziness when it comes to reading, then you might say:</p>

    <div class="alert alert-success font-italic">I know that reading is important, but Indonesian people <span class="font-weight-bold text-success">can't be bothered to</span> read.</div>

    <p>But I urge you to think again about reading. It's an essential skill in IELTS and in university. Practice it and it will become easier and more enjoyable!</p>

