    <h3>Spend money (on someone/something)</h3>

    <p>The preposition you need in English is <b><i>spend on</i></b>:</p>

    <div class="alert alert-success font-italic">Governments have <span class="font-weight-bold text-success">spent</span> quite a lot <span class="font-weight-bold text-success">on</span> space exploration using public money.</div>

    <p>(examples <a href="https://skell.sketchengine.eu/#result?lang=en&query=spend%20on&f=concordance" target="_blank" rel="noopener">here</a>)</p>

    <h3>Spend money (doing something)</h3>

    <p>Another way to express this is using <b><i>__ing</i></b>:</p>

    <div class="alert alert-success font-italic">Governments have <span class="font-weight-bold text-success">spent</span> quite a lot of public money <span class="font-weight-bold text-success">exploring</span>Â space.</div>

    <h3>Spend money (somewhere)</h3>

    <p>You can also spend money in a particular place:</p>

    <div class="alert alert-success font-italic">Governments have <span class="font-weight-bold text-success">spent</span> quite a lot of public money <span class="font-weight-bold text-success">in developing countries</span>.</div>

    <p>In this case, of course other prepositions of place will be possible - <b><i>around</i></b>, <b><i>at</i></b>, etc.</p>

    <h3>Spend time</h3>

    <p>The language used to talk about spending money also applies to spending time, except that we don't often spend time <b><i>on</i></b> someone, rather we spend time <b><i>with</i></b> them:</p>

    <div class="alert alert-success font-italic">Last weekend I <span class="font-weight-bold text-success">spent</span> some <span class="font-weight-bold text-success">time with</span> my girlfriend. We <span class="font-weight-bold text-success">spent</span> Saturday evening <span class="font-weight-bold text-success">at the cinema</span> and after that we <span class="font-weight-bold text-success">spent</span> some <span class="font-weight-bold text-success">time walking</span> along the beach. She noticed that I had gained weight and urged me to <span class="font-weight-bold text-success">spend</span> more <span class="font-weight-bold text-success">time on physical exercise</span>.</div>

