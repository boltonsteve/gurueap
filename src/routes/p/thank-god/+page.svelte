    <p>This is when you are grateful for something. You may be alone, or you may be together with others, but you feel a need to express your gratitude, even out loud. You may be addressing God (who may, or may not be present), or you may be addressing nobody in particular. Indonesians might want to say <b><i>syukur</i></b> or <b><i>alhamdullilah</i></b>, or <b><i>puji tuhan</i></b>. In this case you would say <b><i>Thank God</i></b> (without ‘s’).</p>

    <div class="alert alert-success font-italic">I just heard that I achieved 7.0 overall in my recent IELTS test. <span class="font-weight-bold text-success">Thank God!</span></div>

    <p>You can think of this as an instruction beginning with the imperative verb <b><i>Thank</i></b>.</p>

    <p>You can also think of it as advice: <i>You/We should all <b>thank</b> God!</i></p>

    <p>If you are lucky enough to be face to face with God, and if God has helped you in some way, then you can also look God in the eye and say: <b><i>Thanks, God.</i></b> (with ‘s’ on ‘thank’) However, this is quite informal, and may not be an appropriate way to address someone as important as God. If you want to be more polite when addressing God directly, say: <b><i>Thank you, God</i></b>. (no ‘s’ on ‘thank’)</p>

