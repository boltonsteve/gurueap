    <h1>Himself, Herself, <i>Their</i>..selves(?)</h1>

    <div class="alert alert-danger font-italic">Before departing for Australia, students must prepare <span class="font-weight-bold text-danger">theirselves</span> in order to avoid culture shock.</div>

    <p><b><i>..selves</i></b>. More than one ‘self’. OK, no complaints about that.</p>

    <p><b><i>..theirselves</i></b>. Now you’re being inconsistent with your object pronouns.</p>

    <p>You guys have no problem producing the following:</p>

    <ul>
        <li>He loves <span class="font-weight-bold text-success">her</span>.</li>
        <li>She loves <span class="font-weight-bold text-success">him</span>.</li>
        <li>Their parents love <span class="font-weight-bold text-success">them</span>.</li>
    </ul>

    <p>And you would never write:</p>

    <ul>
        <li>Their parents love <span class="font-weight-bold text-danger">their</span>.</li>
        <li>Look at <span class="font-weight-bold text-danger">their</span>!</li>
        <li>They say they love each other and I believe <span class="font-weight-bold text-danger">their</span>.</li>
    </ul>

    <p>So why the sudden switch to a subject possessive <b><i>their</i></b>?! Please use the object pronoun (him, her, them) + ‘self/selves’:</p>

    <div class="alert alert-success font-italic">Before departing for Australia, students must prepare <span class="font-weight-bold text-success">themselves</span> in order to avoid culture shock.</div>

    <p>And you might also think about some collocation (prepare + s.o./s.th. + for + s.th.):</p>

    <div class="alert alert-success font-italic">Before departing for Australia, students must prepare <span class="font-weight-bold text-success">themselves</span> for culture shock.</div>

    <p>Finally, we can assume that the students must prepare <b><i>themselves</i></b> and not other people, so strictly speaking <b><i>themselves</i></b> is redundant:</p>

    <div class="alert alert-success font-italic">Before departing for Australia, students must prepare for culture shock.</div>

