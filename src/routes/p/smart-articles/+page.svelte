    <h1>The smartest use of articles</h1>

    <h3>First some fun with vocab - <b><i>Smart</i></b> and <b><i>Article</i></b></h3>

    <img src="../img/bond.png" class="img-fluid mx-auto mb-0" alt="smartest remote" style="width:150px;float:right;">

    <p>Before we get into the grammatical meaning of articles, it's interesting to note that in British English <b><i>smart</i></b> is more commonly used to mean <b><i>elegantly dressed</i></b>.</p>

    <p>In American English, meanwhile, <b><i>smart</i></b> usually means <b><i>intelligent</i></b>, hence the term <b><i>smartphone</i></b>, although a lot of smartphones these days also look <b><i>smart</i></b>!</p>

    <p>Meanwhile, <b><i>Article</i></b> is also used in connection with clothing. We can talk about an <b><i>article of clothing</i></b> just as we can talk about an <b><i>item of clothing</i></b>. And so <b><i>article</i></b> is sometimes a useful word for classifying clothes.</p>

    <p>An <b><i>article</i></b> can also be a piece of writing in a magazine or newspaper!</p>

    <p>But let's get back to <b><i>articles</i></b> and grammar - <b><i>a[n], the, zero article</i></b></p>

    <p>I was browsing through DIGG this morning and saw an interesting headline – interesting because it demonstrates two important functions of articles.</p>

    <img src="../img/smartest-remote.png" class="img-fluid mx-auto mb-0 border border-secondary" alt="smartest remote" style="width:75%;">

    <h3 class="font-italic">This isn't <span class="uli">a</span> smart remote</h3>

    <p>If we say <b><i><span class="uli">a</span> smart remote</i></b>, we're not talking about one remote in particular. We're talking in this case about a hypothetical remote – <b><i>one of many</i></b>.</p>

    <p class="mb-2">This use of the indefinite article <b><i>a</i></b> to talk about one of many is extremely common in English, but is often neglected by students, especially by Indonesian students who do not have good translations for articles in their own language.</p>

    <WithIcon icon="flag" position="right" msg="Indonesian students either omit the article completely or use a strategy from their first language to communicate <b><i>one of many</i></b>, usually translating directly from <b><i>salah satu</i></b> (one of), or simply <b><i>satu</i></b> (one)." />

    <h3 class="font-italic">It's <span class="uli">the</span> smartest remote</h3>

    <p>If we say it's <b><i><span class="uli">the</span> smartest remote</i></b> then we're talking about <b><i>this one exactly</i></b>, without comparison.</p>

    <p>Clearly the advertisers, or journalists, want us to think of this smartphone as somehow unique. It's <span class="uli">not</span> <b><i>one of many</i></b>, it's <b><i>this one exactly</i></b>.</p>

    <h3>One last time for good measure..</h3>

    <ul>
        <li>one of many – <b><i>a</i></b></li>
        <li>this one exactly – <b><i>the</i></b></li>
    </ul>

    <p>Try to use these articles in your writing and speaking to communicate these meanings accurately. Then watch as your IELTS grammar scores begin to increase!</p>

