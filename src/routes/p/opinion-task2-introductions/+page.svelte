    <h1>Opinion in Task 2 Introductions</h1>

    <div class="alert alert-danger font-italic"><span class="font-weight-bold text-success">Some people argue that</span> taking a year break causes hesitation to continue study permanently. <span class="font-weight-bold text-success">However</span>, <span class="font-weight-bold text-danger">many people claim that</span> taking a gap year between finishing high school and starting university studies <span class="font-weight-bold text-success">is beneficial</span>, for the following reasons.</div>

    <p>If you want a good score for Task Response in IELTS writing (see <a href="../posts/ielts-descriptors" target="_blank">public band descriptors for Task 2</a>) then you need to make sure your opinion is clear in every paragraph - including your introduction.</p>

    <p>Most of the time in academic writing we try to make our own opinions look strong and other people's appear weak. We can achieve this in 3 ways:</p>

    <h3>1. Positioning</h3>

    <p>Position other people's ideas before your own and add a contrasting signalÂ to show that your own idea is coming next.</p>

    <p>In our opening example, our writer uses effective positioning, beginning with someone else's idea before giving us her own, and she uses a contrasting signal - <b><i>however</i></b> - to confirm that it's her idea next.</p>

    <p>So far so good!</p>

    <h3>2. Evaluative language</h3>

    <p>Use negative evaluative language to talk about other people's ideas and positive evaluative language to talk about ours.</p>

    <p>There's nothing particularly negative about the language used to describe opposite opinion in our opening example:</p>

    <p class="font-italic ml-3">taking a year break causes hesitation to continue study permanently</p>

    <p>..but that's OK - positioning already makes this claim appear weak. Then the writer strengthens her own idea with an extremely positive evaluative adjective - <b><i>beneficial</i></b>.</p>

    <p>So far so good!</p>

    <h3>3. Problematising</h3>

    <p>When we introduce other people's ideas, we use <b><i>problematising phrases</i></b> that show that there might be something wrong with their ideas.</p>

    <p>Our writer uses a problematising phrase - <b><i>some people argue that</i></b> - to introduce opposite opinion and make it appear weak. Great! But then..</p>

    <img src="/img/sinking.jpg" class="mx-auto border rounded" alt="sinking ship">

    <p>..she uses another problematising phrase - <b><i>many people claim that</i></b> - to introduce her own idea!? This is supposed to be your opinion, <span class="uli">not</span> many people's!</p>

    <WithIcon icon="warning" msg="Never use problematising phrases to introduce your own opinion!" />

    <h3>Summary</h3>

    <p>If we take out that second - <i>confusing</i> - problematising phrase, then we're left with a strong introduction to this argument about gap years. The writer's opinion is now obvious, and the reader can look forward to some supporting arguments in the body paragraphs.</p>

    
