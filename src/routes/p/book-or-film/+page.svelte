    <h1>Book or film?</h1>

    <p>My students often tell me that they would much rather watch a film than read a book. Reading is boring, they say.</p>

    <p>I would like to invite you to think again about reading.</p>

    <p>My teaching colleagues and I would all agree that reading novels is fun, and we all recommend this kind of <b><i>extensive</i></b> reading to our students. Most of us would also agree that when a film is made based on a book, the book is always much more satisfying than the film that is based on the book.</p>

    <p>Let’s try an experiment and see which you prefer – the book or the film? First you’re going to read and listen to a short text. Then you’re going to watch a video clip based on the same text. Finally you’ll reflect on the experience and think again about which you prefer – reading or just watching.</p>

    <h3>1. Reading</h3>

    <p>Read the text shown in the clip below and use your imagination to picture what’s going on in the ‘story’. Think carefully about the imagery and about characters in the story. What do the people in the story look like? Where are they?</p>

    <Youtube id="gg5-lCg5UG4" />

    <h3>2. Watching</h3>

    <p>Now watch the <b><i>movie</i></b>. Compare what you see in the film to what you saw in your mind as you were reading. Did you ‘see’ the same things? How are the images in the video different from the images you saw in your mind when you were reading?</p>

    <Youtube id="u3bVjadfXD4" />

    <h3>3. Reflection</h3>

    <p>So what do you think? Do you still prefer watching somebody else’s thoughts. Who is the best ‘director’? <i>You</i> when you read? Or <i>someone else</i> when <i>they</i> read?</p>

