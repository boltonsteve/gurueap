    <h1>In terms of</h1>

    <p class="font-italic alert alert-danger">Google also increased steadily, although not as significantly as Facebook.</p>

    <p>This writer makes us extremely curious. What does it mean that Google <i>increased</i> steadily?</p>

    <ul>
        <li>Did Google and Facebook get fatter?</li>
        <li>Did they multiply?</li>
        <li>Did their profits increase?</li>
        <li>Did the amount of detail on their pages increase?</li>
        <li>Did the number of users increase?</li>
    </ul>

    <p class="mb-0">There's a magic phrase you can use in IELTS writing that will help you to make yourself clear in this situation:</p>

<p class="text-center m-0 p-0"><span style="font-size:450%;">in terms of</span></p>

    <p class="font-italic alert alert-success mt-0">Google also <span class="font-weight-bold text-success">increased</span> steadily <span class="font-weight-bold text-success">in terms of</span> <span class="uli">the number of users</span>, although not as significantly as Facebook.</p>

    <p>Here the reader can see clearly the thing that changed - <b><i>the number of users</i></b>.</p>

<h3>How to use it in Task 1?</h3>

<p class="font-weight-bold text-center">in terms of + the statistical unit being described</p>

<p class="small">In IELTS Task 1 writing the <b><i>statistical units</i></b> are usually given on the x and y axes of a graph, or somewhere close to a chart or table.</p>

<h3 class="mb-0">How to use it in Task 2?</h3>
<p class="mt-0">Use <b><i>in terms of</i></b> in Task 2 to introduce the topic of a body paragraph:</p>

<p class="font-italic ml-3">Shopping online is better than shopping in physical stores <b><i>in terms of</i></b> <span class="uli">convenience</span>. <span class="text-secondary"><b>First of all</b>, adipisicing corrupti necessitatibus nemo minus nostrum, exercitationem. <b>In addition</b>, asperiores qui vero exercitationem hic ipsum qui. In consequatur qui accusantium placeat fugiat Rem perferendis possimus autem quisquam recusandae. <b>Moreover</b>, dolore praesentium dignissimos beatae dolorum in. Praesentium temporibus placeat cum aliquid consectetur, quibusdam animi.</span></p>

<h3 class="mb-0">IELTS Scores</h3>
<p class="mt-0"><b><i>In terms of</i></b> is a lexical phrase in which all three words <a href="../posts/collocation">collocate</a> strongly. Strong <a href="../posts/collocation">collocation</a> in IELTS gets you a high score for vocabulary, and since it makes your writing clearer, your scores for <a href="../posts/ielts-descriptors">Task Achievement (TA)</a> and also for <a href="../posts/ielts-descriptors">Coherence and Cohesion (CC)</a> will also increase!</p>

