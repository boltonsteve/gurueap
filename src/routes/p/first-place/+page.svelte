    <h1>In (the) first place</h1>

    <div class="alert alert-danger font-italic"><span class="font-weight-bold text-danger">In the first place</span> is over-grazing, which caused 35% of land degradation.</div>

    <p>Not a terrible ‘error’ - we know what you mean! But still, it’s important to understand the distinction between <b><i>in first place</i></b> and <b><i>in the first place</i></b>.</p>

    <p>In IELTS Task 1 writing we often find ourselves ranking items as follows:</p>

    <div class="alert alert-success font-italic"><span class="font-weight-bold text-success">In first place</span> is over-grazing, which caused 35% of land degradation. Meanwhile <span class="font-weight-bold text-success">in second place</span>, 20% of land degradation was caused by deforestation.</div>

    <p>But what if you’re listing rather than ranking? Let's say, for example, that you're listing supports for an argument. In this case you need <b><i>in the first place</i></b>, <b><i>in the second place</i></b>, etc.:</p>

    <div class="alert alert-success font-italic">Mr Jones cannot be the one who stole your car. <span class="font-weight-bold text-success">In the first place</span> he was in a different city when the car was stolen, and secondly he is blind!</div>

    <p>In this case <b><i>in the first place</i></b> means as the first consideration. It's often used to introduce reasons that should be obvious but may need to be emphasised, as in the above example. Notice that it is unusual to continue <b><i>in the second place</i></b>, <b><i>in the third place</i></b>, etc. Better to switch to <b><i>secondly</i></b>, <b><i>thirdly</i></b>, and so on.</p>

<p>To sum up..</p>

<ul>
	<li>'In first place..' is useful in Task 1 writing (for ranking)</li>
	<li>'In <i><b>the</b></i> first place..' is useful in Task 2 writing (for emphasising reasons)</li>
</ul>

