    <h1>Reaching a peak in IELTS Task 1</h1>

    <div class="alert alert-danger font-italic">In general, the 6pm news <span class="font-weight-bold text-danger">reached its peak</span> for almost 5 million viewers per day in the first month.</div>

    <p>This is actually quite communicative and in IELTS this sentence might give you a satisfactory score for TA. However, the language problems would leave you with a much lower score for GRA and for CC. (What are TA, CC and GRA?)</p>

    <ol>
        <li>It doesn’t make sense to signal this statement <b><i>In general</i></b>, because it’s not general. It features data values taken from the x and y axes of a line graph. Better to put this information in the detail section of your essay and signal it <b><i>In detail</i></b>.</li>
        <li>You need to treat <b><i>reach a peak</i></b> as a phrasal verb. If you want to change the tense – and the tense will most likely be past simple tense – then you can modify <b><i>reach</i></b> (past: <b><i>reached</i></b> a peak). Otherwise don’t mess with <b><i>a</i></b> and don’t mess with <b><i>peak</i></b>.</li>
        <li>The preposition ‘for’ is not right.</li>
    </ol>

    <p>If you really are making a <i>general</i> statement, do this:</p>

    <div class="alert alert-success font-italic">In general, the popularity of the 6pm news <span class="font-weight-bold text-success">reached a peak</span> in the first month.</div>

    <p>If you want to mention detail, then do this:</p>

    <div class="alert alert-success font-italic">In detail, the 6pm news <span class="font-weight-bold text-success">reached a peak</span> <span class="uli">of almost 5 million viewers per day</span> in the first month.</div>

    <p>Pay careful attention to this pattern:</p>

    <p class="pl-3"><b><i>something + reached a peak (+ of + value x) (+ time expression)</i></b></p>

    <p>If you don’t believe me, check out <a href="https://skell.sketchengine.eu/#result?lang=en&query=reached%20a%20peak&f=concordance" target="_blank">these examples</a>!</p>

    <p>And whatever you do, don’t write <b><i>reached <span class="uli">the</span> peak</i></b>! That applies to climbers only, where <b><i>the</i></b> refers to the mountain they happen to be climbing at the time!</p>

