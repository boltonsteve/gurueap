    <p>The word <b><i>elder</i></b> has quite specific meanings:</p>

    <h3>1. Elderly</h3>

    <img src="/img/elderly-couple.jpg" class="img-fluid mx-auto my-1 border" alt="elderly couple" style="width:75%;">
    <p class="text-center small mt-0">elderly couple</p>

    <p>In the above photo we see an <b><i>elderly couple</i></b>. Both man and wife are retired. They do not work. They may be beginning to experience some health problems and they may even have difficulty walking. They are probably in their late seventies or older. These people are <b><i>elderly</i></b>.</p>

    <h3>2. Elder(s)</h3>

    <img src="/img/elders-meeting.jpg" class="img-fluid mx-auto my-1 border" alt="elders meeting" style="width:75%;">
    <p class="text-center small mt-0">meeting with village elders</p>

    <p>In this next photo we see an army officer meeting with senior members of a village council. The council members have been chosen by the community to represent them when important decisions need to be taken about the future of the village and the welfare of the community in general. These people are <b><i>elders</i></b>.</p>

    <h3>3. Elder(s)</h3>

    <img src="/img/elder-child.jpg" class="img-fluid mx-auto my-1 border" alt="elder child" style="width:75%;">
    <p class="text-center small mt-0">elder child</p>

    <p>And in this final picture an older student is assisting a younger student with his reading practice. In this case the older student is the <b><i>elder</i></b> of the younger student. At school we often consult our <b><i>elders</i></b> when we need help with our homework, etc.</p>

