            <p class="mt-0 font-italic">The <span class="font-weight-bold text-success">demands of</span> IELTS are considerable.</p>
            <p class="mb-0">IELTS is hard work/difficult.</p>
        </div>

        <h3>demand + for</h3>

        <p>People need something:</p>

        <div class="alert alert-success mb-2">
            <p class="mt-0 font-italic"><span class="font-weight-bold text-success">Demand for</span> oil field services has been very strong.</p>
            <p class="mb-0">A lot of people want or need oil field services.</p>
        </div>
        <div class="alert alert-success mb-2">
            <p class="mt-0 font-italic"><span class="font-weight-bold text-success">Demand for</span> paper products softened slightly in recent weeks.</p>
            <p class="mb-0">Recently, fewer people have needed paper products.</p>
        </div>

