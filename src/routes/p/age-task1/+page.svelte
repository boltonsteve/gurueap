    <p>I know, this seems to make sense, and your meaning is clear, but if you want a high score for vocabulary you need to use better collocation inside your noun phrases:</p>

    <div class="alert alert-success font-italic mb-2">People in the 14-17 <span class="font-weight-bold text-success">age group</span> were most affected by the changes.</div>

    <WithIcon icon="flag" position="right">
        <div slot="html">Indonesian candidates often translate directly <b><i>kelompok umur</i></b> as <b><i>group of age</i></b>. Although communicative, this is weak <a href="http://gurueap.com/posts/collocation" target="_blank">collocation</a> in English.</div>
    </WithIcon>

    <p>The following are all possible. Unfortunately you just have to memorise them. Try to use them as soon as possible in your writing practice and soon they will become automatic.</p>

    <ul>
        <li>a person who is 49 years of age</li>
        <li>a person who is 49 years old</li>
        <li>a person who is 49</li>
        <li>a 49 year-old person</li>
        <li>a 49 year-old</li>
    </ul>

    <p>More often in IELTS, <b><i>person</i></b> is plural (= <b><i>people</i></b>), and so the following noun phrases are also possible:</p>

    <ul>
        <li>49 year-olds</li>
        <li>49 year-old people</li>
        <li>people in the 50-60 age group</li>
        <li>people aged between 50 and 60</li>
        <li>people aged 50-60</li>
        <li>people (who are) 50-60 years old</li>
    </ul>

    <p>Of course we could be more specific about <b><i>person</i></b> or <b><i>people</i></b>:</p>

    <ul>
        <li>A 49 year-old <b>English teacher</b></li>
        <li><b>English teachers</b> in the 49-59 age group</li>
    </ul>

